SOUNDNESS_PROOF = r"""
Theorem (D8 Section Merging).

Let T = case(g, A, case(g', B, C)) be a nested case expression.

Case 1 — Complementary guards (g' = ¬g):
  In the else-branch of g (i.e. ¬g holds), g' = ¬g = True.
  Therefore case(g', B, C) evaluates to B.
  Result: case(g, A, B).

Case 2 — Subsumed guard (g implies g'):
  When ¬g holds, if g' is a disjunct of g or logically
  implied by g, then g' is guaranteed False in the else-branch.
  Therefore case(g', B, C) evaluates to C.
  Result: case(g, A, C).

Case 3 — Identical guard (g' = g):
  In the else-branch of g, g is False (by construction).
  Therefore case(g, B, C) evaluates to C.
  Result: case(g, A, C).  (Same as Case 2.)

Case 4 — Disjoint guards reordering:
  When g ∧ g' = ⊥ (guards are disjoint), the order of
  evaluation does not affect the result. We can swap:
  case(g, A, case(g', B, C)) ↔ case(g', B, case(g, A, C)).
  □
"""

