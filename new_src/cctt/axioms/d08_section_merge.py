"""D8 Axiom: Section / Guard Merging.

Simplifies OCase chains by merging, reordering, and eliminating
guards that are complementary, subsumed, or identical.

Mathematical basis: In the sheaf-theoretic view, each guard
defines an open set U_i on which a section s_i is defined.
If U_i and U_j are complementary (U_i ∪ U_j = X and U_i ∩ U_j = ∅),
then the case expression case(g_i, s_i, case(g_j, s_j, s_k)) can
be simplified to case(g_i, s_i, s_j) because g_j is True whenever
g_i is False — the sections glue uniquely on the cover.

Guard subsumption detects when an inner guard is logically
implied by the negation of the outer guard, allowing the inner
guard to be eliminated (the branch is unreachable in the else
of the outer guard).

This is the sheaf descent condition applied to finite covers
generated by boolean predicates (§10, Definition 4.10).
"""
