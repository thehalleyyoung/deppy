_COMP_COMPLEMENTS: FrozenSet[Tuple[str, str]] = frozenset({
    ('lt', 'gte'), ('gte', 'lt'), ('gt', 'lte'), ('lte', 'gt'),
    ('eq', 'noteq'), ('noteq', 'eq'), ('ne', 'eq'), ('eq', 'ne'),
    ('le', 'gt'), ('gt', 'le'), ('ge', 'lt'), ('lt', 'ge'),
})


def _guards_complementary(g1: OTerm, g2: OTerm) -> bool:
    """Check if two guards are logical complements: g1 ≡ ¬g2."""
    # Explicit negation
    if isinstance(g1, OOp) and g1.name == 'u_not' and len(g1.args) == 1:
        return g1.args[0].canon() == g2.canon()
    if isinstance(g2, OOp) and g2.name == 'u_not' and len(g2.args) == 1:
        return g2.args[0].canon() == g1.canon()
    # Comparison complements
    if (isinstance(g1, OOp) and isinstance(g2, OOp)
            and len(g1.args) == 2 and len(g2.args) == 2
            and (g1.name, g2.name) in _COMP_COMPLEMENTS
            and g1.args[0].canon() == g2.args[0].canon()
            and g1.args[1].canon() == g2.args[1].canon()):
        return True
    return False


def _guards_disjoint(g1: OTerm, g2: OTerm) -> bool:
    """Conservative disjointness check: g1 ∧ g2 = ⊥."""
    if _guards_complementary(g1, g2):
        return True
    if isinstance(g1, OOp) and isinstance(g2, OOp):
        comp_pairs = {('lt', 'gte'), ('gt', 'lte'), ('lte', 'gt'), ('gte', 'lt')}
        if (g1.name, g2.name) in comp_pairs:
            if len(g1.args) == 2 and len(g2.args) == 2:
                if (g1.args[0].canon() == g2.args[0].canon()
                        and g1.args[1].canon() == g2.args[1].canon()):
                    return True
        # Distinct equality checks on same variable
        if g1.name == 'eq' and g2.name == 'eq':
            if len(g1.args) == 2 and len(g2.args) == 2:
                if g1.args[0].canon() == g2.args[0].canon():
                    if (isinstance(g1.args[1], OLit) and isinstance(g2.args[1], OLit)
                            and g1.args[1].value != g2.args[1].value):
                        return True
    return False


def _guard_subsumed(outer_guard: OTerm, inner_guard: OTerm) -> bool:
    """Check if inner_guard is subsumed by outer_guard.

    Returns True when: in the else-branch of outer_guard,
    inner_guard is guaranteed to be False.
    """
    ig_c = inner_guard.canon()
    # Collect disjuncts of outer guard
    disjuncts = _collect_disjuncts(outer_guard)
    for d in disjuncts:
        if d.canon() == ig_c:
            return True

    # Dictionary-specific: notin(k,d) in outer →
    # is(.get(d,k), None) is False in else-branch
    if isinstance(inner_guard, OOp) and inner_guard.name == 'is':
        if len(inner_guard.args) == 2 and isinstance(inner_guard.args[1], OLit):
            if inner_guard.args[1].value is None:
                inner_x = inner_guard.args[0]
                for d in disjuncts:
                    if isinstance(d, OOp) and d.name == 'notin' and len(d.args) == 2:
                        if (isinstance(inner_x, OOp) and inner_x.name == '.get'
                                and len(inner_x.args) == 2):
                            if (inner_x.args[0].canon() == d.args[1].canon()
                                    and inner_x.args[1].canon() == d.args[0].canon()):
                                return True
                    if isinstance(d, OOp) and d.name == 'is' and len(d.args) == 2:
                        if isinstance(d.args[1], OLit) and d.args[1].value is None:
                            if d.args[0].canon() == inner_x.canon():
                                return True
    return False


def _collect_disjuncts(guard: OTerm) -> List[OTerm]:
    """Collect all disjuncts from an or(...) guard."""
    if isinstance(guard, OOp) and guard.name == 'or':
        result: List[OTerm] = []
        for a in guard.args:
            result.extend(_collect_disjuncts(a))
        return result
    return [guard]


def _collect_case_chain(term: OTerm) -> List[Tuple[OTerm, OTerm]]:
    """Flatten right-nested OCase chain into (guard, value) pairs."""
    chain: List[Tuple[OTerm, OTerm]] = []
    cur = term
    while isinstance(cur, OCase):
        chain.append((cur.test, cur.true_branch))
        cur = cur.false_branch
    chain.append((OLit(True), cur))
    return chain


def _build_case_chain(chain: List[Tuple[OTerm, OTerm]]) -> OTerm:
    """Reconstruct right-nested OCase from a chain list."""
    if len(chain) == 1:
        return chain[0][1]
    guard, val = chain[0]
    rest = _build_case_chain(chain[1:])
    return OCase(guard, val, rest)


# ═══════════════════════════════════════════════════════════
# Main axiom: apply
# ═══════════════════════════════════════════════════════════

def apply(term: OTerm, ctx: FiberCtx) -> List[Tuple[OTerm, str]]:
    """D8: Section/guard merging axiom.

    Handles:
      1. Complementary guard merging: case(g, A, case(¬g, B, C)) → case(g, A, B)
      2. Subsumed guard elimination: case(g, A, case(g_sub, B, C)) → case(g, A, C)
      3. Identical guard elimination: case(g, A, case(g, B, C)) → case(g, A, C)
      4. Disjoint guard reordering (commutation of sections)
      5. Not-guard flipping: case(¬g, A, B) → case(g, B, A)
      6. Guard chain permutation for 3+ disjoint guards
      7. True-branch complementary merge
    """
    results: List[Tuple[OTerm, str]] = []

    if not isinstance(term, OCase):
        return results

    # ── Not-guard flipping ──
    if isinstance(term.test, OOp) and term.test.name == 'u_not' and len(term.test.args) == 1:
        flipped = OCase(term.test.args[0], term.false_branch, term.true_branch)
        results.append((flipped, 'D8_not_flip'))

    # ── Nested case in false branch ──
    if isinstance(term.false_branch, OCase):
        inner = term.false_branch

        # Complementary guards
        if _guards_complementary(term.test, inner.test):
            results.append((
                OCase(term.test, term.true_branch, inner.true_branch),
                'D8_section_merge',
            ))

        # Subsumed guard
        if _guard_subsumed(term.test, inner.test):
            results.append((
                OCase(term.test, term.true_branch, inner.false_branch),
                'D8_section_subsumed',
            ))

        # Identical guard
        if term.test.canon() == inner.test.canon():
            results.append((
                OCase(term.test, term.true_branch, inner.false_branch),
                'D8_section_dup',
            ))

        # Disjoint guard reordering
        if _guards_disjoint(term.test, inner.test):
            swapped = OCase(inner.test, inner.true_branch,
                            OCase(term.test, term.true_branch, inner.false_branch))
            if swapped.canon() != term.canon():
                results.append((swapped, 'D8_section_reorder'))

    # ── Nested case in true branch (complementary) ──
    if isinstance(term.true_branch, OCase):
        inner = term.true_branch
        if _guards_complementary(term.test, inner.test):
            results.append((
                OCase(term.test, inner.false_branch, term.false_branch),
                'D8_section_merge_true',
            ))

    # ── Chain permutation for 3+ disjoint guards ──
    chain = _collect_case_chain(term)
    guards = [g for g, _ in chain[:-1]]
    if len(guards) >= 3:
        all_disjoint = all(
            _guards_disjoint(guards[i], guards[j])
            for i in range(len(guards))
            for j in range(i + 1, len(guards))
        )
        if all_disjoint:
            # Rotate guards by one position
            rotated = chain[1:len(chain) - 1] + [chain[0]] + [chain[-1]]
            rebuilt = _build_case_chain(rotated)
            if rebuilt.canon() != term.canon():
                results.append((rebuilt, 'D8_section_rotate'))

    return results


# ═══════════════════════════════════════════════════════════
# Recognizer & scoring
# ═══════════════════════════════════════════════════════════

def recognizes(term: OTerm) -> bool:
    """Quick check: could D8 apply to this term?"""
    if not isinstance(term, OCase):
        return False
    # Not-guard
    if isinstance(term.test, OOp) and term.test.name == 'u_not':
        return True
    # Nested case in either branch
    if isinstance(term.false_branch, OCase):
        return True
    if isinstance(term.true_branch, OCase):
        return True
    return False


def relevance_score(term: OTerm, other: OTerm) -> float:
    """Heuristic 0.0–1.0 for how likely D8 helps bridge term→other."""
    if not isinstance(term, OCase):
        return 0.0
    score = 0.0
    # Complementary guards are a strong signal
    if isinstance(term.false_branch, OCase):
        inner = term.false_branch
        if _guards_complementary(term.test, inner.test):
            score = max(score, 0.95)
        elif term.test.canon() == inner.test.canon():
            score = max(score, 0.9)
        elif _guards_disjoint(term.test, inner.test):
            score = max(score, 0.7)
        else:
            score = max(score, 0.3)
    # Not-guard is easy to flip
    if isinstance(term.test, OOp) and term.test.name == 'u_not':
        score = max(score, 0.8)
    # Target has fewer case levels → merging likely helps
    if isinstance(other, OCase):
        t_depth = _case_depth(term)
        o_depth = _case_depth(other)
        if t_depth > o_depth:
            score = max(score, 0.6 + 0.1 * (t_depth - o_depth))
    elif not isinstance(other, OCase):
        # term is a case but target is not — simplification needed
        score = max(score, 0.5)
    return min(score, 1.0)


def _case_depth(term: OTerm) -> int:
    """Count the nesting depth of OCase in the false branch."""
    depth = 0
    cur = term
    while isinstance(cur, OCase):
        depth += 1
        cur = cur.false_branch
    return depth


# ═══════════════════════════════════════════════════════════
# Inverse
# ═══════════════════════════════════════════════════════════

def apply_inverse(term: OTerm, ctx: FiberCtx) -> List[Tuple[OTerm, str]]:
    """Reverse D8: split merged guards or introduce redundant guards.

    The inverse of section merging re-introduces an inner guard
    that is complementary to the outer guard, expanding:
        case(g, A, B) → case(g, A, case(¬g, B, ⊥))
    """
    results: List[Tuple[OTerm, str]] = []

    if isinstance(term, OCase):
        # Introduce complementary inner guard
        neg_guard = OOp('u_not', (term.test,))
        expanded = OCase(
            term.test,
            term.true_branch,
            OCase(neg_guard, term.false_branch, OUnknown('_unreachable')),
        )
        results.append((expanded, 'D8_inv_split_complementary'))

        # Duplicate guard (introduce redundant)
        dup = OCase(
            term.test,
            term.true_branch,
            OCase(term.test, OUnknown('_dead'), term.false_branch),
        )
        results.append((dup, 'D8_inv_introduce_dup_guard'))

    return results


