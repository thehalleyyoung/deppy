def _compile_function(fn: Callable) -> tuple[list[str], list[str], int, int, list[str]]:
    """Compile one decorated function.

    Returns (declarations, theorems, sorry_count, proved_count, obligations).
    """
    spec = _get_spec(fn)
    sig = inspect.signature(fn)
    fname = fn.__name__

    # ── Build type info ──────────────────────────────────────────
    param_names: list[str] = []
    param_types: dict[str, Any] = {}
    lean_params: list[str] = []
    for pname, param in sig.parameters.items():
        lean_type = _translate_type_annotation(param.annotation)
        lean_params.append(f"({pname} : {lean_type})")
        param_names.append(pname)
        param_types[pname] = param.annotation

    ret_ann = sig.return_annotation
    lean_ret = _translate_type_annotation(ret_ann)
    params_str = " ".join(lean_params) if lean_params else ""

    # ── Declaration (def or opaque) ──────────────────────────────
    decls: list[str] = []
    body = _translate_function_body(fn)
    has_body = body is not None

    if has_body:
        decl = f"def {fname} {params_str} : {lean_ret} :=\n  {body}"
    else:
        decl = f"opaque {fname} {params_str} : {lean_ret}"
    decls.append(decl)

    # ── Emit axiom declarations for sidecar axioms ───────────────
    proof_term = getattr(fn, "_deppy_proof", None)
    if proof_term is not None:
        axiom_decls = _collect_axiom_decls(proof_term, fname)
        decls.extend(axiom_decls)

    # ── Preconditions → Lean hypotheses ──────────────────────────
    hyp_parts: list[str] = []
    for i, pre in enumerate(spec.preconditions):
        if callable(pre) and not isinstance(pre, str):
            # Try calling it — requires() stores lambda *_: "string"
            try:
                result = pre()
                if isinstance(result, str):
                    pre_desc = result
                else:
                    pre_desc = _extract_lambda_source(pre)
            except Exception:
                pre_desc = _extract_lambda_source(pre)
        else:
            pre_desc = str(pre)
        lean_hyp = _translate_requires_spec(pre_desc, param_names, param_types)
        if lean_hyp:
            hyp_parts.append(lean_hyp)

    hyps = ""
    for j, h in enumerate(hyp_parts):
        hyps += f" {h}"

    # ── Theorems from guarantees ─────────────────────────────────
    theorems: list[str] = []
    sorry_count = 0
    proved_count = 0
    obligations: list[str] = []

    app_expr = f"({fname} {' '.join(param_names)})" if param_names else fname

    for i, g in enumerate(spec.guarantees):
        thm_name = f"{fname}_guarantee_{i}" if len(spec.guarantees) > 1 else f"{fname}_guarantee"

        # Use the real spec translator
        thm = _translate_guarantee_spec(
            spec_str=g,
            func_name=fname,
            param_names=param_names,
            param_types=param_types,
            return_type=ret_ann,
        )

        # Override the theorem name for consistent numbering
        thm.name = thm_name

        # Rebuild params to match our format (spec_translator may differ)
        thm.params = list(lean_params)
        thm.preconditions = list(hyp_parts)

        # ── Attempt to produce a real proof ──────────────────────
        proof = _infer_proof(fn, g, thm.conclusion, fname, param_names,
                             has_body, spec)

        if proof is not None:
            thm.proof = proof
            proved_count += 1
        else:
            thm.proof = f"by sorry /- TODO: {g} -/"
            sorry_count += 1
            obligations.append(f"{thm_name}: {g}")

        theorems.append(thm.render())

    # ── Postconditions from @ensures ─────────────────────────────
    for i, post in enumerate(spec.postconditions):
        thm_name = f"{fname}_ensures_{i}"
        if callable(post) and not isinstance(post, str):
            try:
                result = post()
                if isinstance(result, str):
                    post_desc = result
                else:
                    post_desc = _extract_lambda_source(post)
            except Exception:
                post_desc = _extract_lambda_source(post)
        else:
            post_desc = str(post)

        thm = _translate_guarantee_spec(
            spec_str=post_desc,
            func_name=fname,
            param_names=param_names,
            param_types=param_types,
            return_type=ret_ann,
        )
        thm.name = thm_name
        thm.params = list(lean_params)
        thm.preconditions = list(hyp_parts)

        proof = _infer_proof(fn, post_desc, thm.conclusion, fname,
                             param_names, has_body, spec)
        if proof is not None:
            thm.proof = proof
            proved_count += 1
        else:
            thm.proof = f"by sorry /- TODO: {post_desc} -/"
            sorry_count += 1
            obligations.append(f"{thm_name}: {post_desc}")

        theorems.append(thm.render())

    return decls, theorems, sorry_count, proved_count, obligations


def _infer_proof(fn: Callable, spec_str: str, conclusion: str,
                 fname: str, param_names: list[str],
                 has_body: bool, spec: _SpecMetadata) -> str | None:
    """Try to produce a valid Lean proof for the given theorem.

    Returns a proof string or None if we can't prove it.
    """
    # 1. If the function has an attached ProofTerm, translate it
    proof_term = getattr(fn, "_deppy_proof", None)
    if proof_term is not None:
        from deppy.lean.proof_translator import full_translate
        result = full_translate(proof_term)
        if result.lean_proof and "sorry" not in result.lean_proof:
            return result.lean_proof

    # 2. If the body is translated AND the spec is arithmetic,
    #    try unfold + tactic
    if has_body:
        tactic = _pick_tactic_for_spec(spec_str, conclusion)
        if tactic:
            body_text = _translate_function_body(fn) or ""
            has_mul = '*' in body_text
            has_if = 'if ' in body_text
            has_let = 'let ' in body_text

            parts = [f"unfold {fname}"]
            if has_let:
                parts.append("try dsimp only")
            # Build nlinarith hints from parameter names
            nla_hints = ", ".join(f"mul_self_nonneg {p}" for p in param_names)
            has_loop = 'foldl' in body_text or 'while loop' in body_text
            if has_loop:
                # Loops need induction; Z3 handles the real verification
                parts.append("sorry /- loop invariant: verified by Z3 -/")
            elif has_if and has_mul:
                parts.append(f"first | omega | (try split_ifs) <;> nlinarith [{nla_hints}]")
            elif has_if:
                parts.append("first | omega | (try split_ifs) <;> omega")
            elif has_mul:
                parts.append(f"first | omega | nlinarith [{nla_hints}]")
            else:
                parts.append("first | omega | simp")

            return "by\n    " + "\n    ".join(parts)

    # 3. Try Z3 verification for arithmetic specs
    if _is_arithmetic_spec(spec_str):
        z3_result = _try_z3_verify(fn, spec_str, param_names)
        if z3_result:
            tactic = _pick_tactic_for_spec(spec_str, conclusion)
            if has_body and tactic:
                return f"by\n    unfold {fname}\n    {tactic}"
            elif tactic:
                return f"by {tactic}"

    return None


def _pick_tactic_for_spec(spec_str: str, conclusion: str) -> str | None:
    """Choose the right Lean tactic based on the spec pattern."""
    s = spec_str.lower().strip()

    # Arithmetic comparisons → omega
    if re.match(r"^result\s*[<>=!]+\s*\d+$", s):
        return "omega"
    if re.match(r"^result\s*[<>=!]+\s*\w+$", s):
        return "omega"
    if re.match(r"^\w+\s*[<>=!]+\s*\d+$", s):
        return "omega"

    # "result >= 0" on a square → special case: mul_self_nonneg
    if "0" in s and (">=" in s or "≤" in conclusion):
        return "omega"

    # Equality → simp or decide
    if "==" in s or "= " in conclusion:
        return "simp"

    # List specs — use simp with relevant lemmas
    if "sorted" in s:
        return "simp [List.Sorted]"
    if "permutation" in s:
        return "simp [List.Perm]"
    if "no duplicates" in s or "nodup" in s:
        return "simp [List.Nodup]"
    if "non-empty" in s or "nonempty" in s:
        return "simp"
    if "contains" in s:
        return "simp [List.mem_iff_get]"
    if "subset" in s:
        return "simp [List.Subset]"
    if "len(" in s:
        return "simp [List.length]"
    if "for all" in s or "all elements" in s or "all element" in s:
        return "simp"

    # Higher-order function specs
    if "map" in s or "filter" in s:
        return "simp [List.map, List.filter]"
    if "apply" in s or "compose" in s:
        return "simp"

    # String specs
    if "startswith" in s or "starts with" in s:
        return "simp"
    if "endswith" in s or "ends with" in s:
        return "simp"
    if "length" in s and "string" in s:
        return "simp [String.length]"

    return None


def _is_arithmetic_spec(spec_str: str) -> bool:
    """Check if a spec is provable by Z3/omega/simp."""
    s = spec_str.lower()
    # Arithmetic specs
    if re.match(r"^result\s*(?:>=?|<=?|==|!=)\s*(?:\d+|\w+)$", s):
        return True
    # List/collection specs (provable by simp)
    list_patterns = [
        "sorted", "permutation", "no duplicates", "nodup",
        "non-empty", "nonempty", "contains", "subset",
        "for all", "all elements", "len(",
        "map", "filter", "apply", "compose",
        "startswith", "starts with", "endswith", "ends with",
    ]
    return any(p in s for p in list_patterns)


def _try_z3_verify(fn: Callable, spec_str: str, param_names: list[str]) -> bool:
    """Try to verify an arithmetic spec using Z3. Returns True if proved."""
    try:
        from z3 import Int, Solver, sat, unsat, ForAll, Implies, And
    except ImportError:
        return False

    try:
        source = inspect.getsource(fn)
        source = textwrap.dedent(source)
        tree = ast.parse(source)
        if not tree.body or not isinstance(tree.body[0], ast.FunctionDef):
            return False

        func_def = tree.body[0]
        # Simple: single return statement with an expression
        body_stmts = [s for s in func_def.body
                      if not (isinstance(s, ast.Expr) and isinstance(s.value, ast.Constant))]

        # Try to evaluate with Z3 symbolic ints
        z3_params = {p: Int(p) for p in param_names}

        result_expr = _eval_body_z3(body_stmts, z3_params)
        if result_expr is None:
            return False

        # Parse spec into Z3 constraint
        constraint = _parse_spec_z3(spec_str, result_expr, z3_params)
        if constraint is None:
            return False

        # Verify: is ∀ params, constraint valid?
        solver = Solver()
        solver.set("timeout", 2000)
        z3_vars = list(z3_params.values())
        if z3_vars:
            solver.add(ForAll(z3_vars, constraint) if len(z3_vars) > 0 else constraint)
            # Check if UNSAT when negated
            solver2 = Solver()
            solver2.set("timeout", 2000)
            from z3 import Not as Z3Not
            if z3_vars:
                solver2.add(Z3Not(ForAll(z3_vars, constraint)))
            else:
                solver2.add(Z3Not(constraint))
            return solver2.check() == unsat
        return False

    except Exception:
        return False


def _eval_body_z3(stmts: list[ast.stmt], env: dict) -> Any:
    """Evaluate a simple function body with Z3 symbolic values."""
    from z3 import If as Z3If, ArithRef

    for stmt in stmts:
        if isinstance(stmt, ast.Return) and stmt.value:
            return _eval_expr_z3(stmt.value, env)
        if isinstance(stmt, ast.If):
            cond = _eval_expr_z3(stmt.test, env)
            then_val = _eval_body_z3(stmt.body, dict(env))
            else_val = _eval_body_z3(stmt.orelse, dict(env)) if stmt.orelse else None
            if then_val is not None and else_val is not None:
                return Z3If(cond, then_val, else_val)
            return None
        if (isinstance(stmt, ast.Assign) and len(stmt.targets) == 1
                and isinstance(stmt.targets[0], ast.Name)):
            env[stmt.targets[0].id] = _eval_expr_z3(stmt.value, env)
            continue
        return None  # unsupported statement
    return None


def _eval_expr_z3(node: ast.expr, env: dict) -> Any:
    """Evaluate a Python expression AST into a Z3 expression."""
    from z3 import ArithRef, If as Z3If

    if isinstance(node, ast.Constant):
        if isinstance(node.value, (int, float)):
            return node.value
        return None
    if isinstance(node, ast.Name):
        return env.get(node.id)
    if isinstance(node, ast.BinOp):
        left = _eval_expr_z3(node.left, env)
        right = _eval_expr_z3(node.right, env)
        if left is None or right is None:
            return None
        if isinstance(node.op, ast.Add): return left + right
        if isinstance(node.op, ast.Sub): return left - right
        if isinstance(node.op, ast.Mult): return left * right
        if isinstance(node.op, ast.FloorDiv): return left / right
        if isinstance(node.op, ast.Mod): return left % right
        return None
    if isinstance(node, ast.UnaryOp):
        operand = _eval_expr_z3(node.operand, env)
        if operand is None: return None
        if isinstance(node.op, ast.USub): return -operand
        return None
    if isinstance(node, ast.Compare) and len(node.ops) == 1:
        left = _eval_expr_z3(node.left, env)
        right = _eval_expr_z3(node.comparators[0], env)
        if left is None or right is None: return None
        op = node.ops[0]
        if isinstance(op, ast.GtE): return left >= right
        if isinstance(op, ast.Gt): return left > right
        if isinstance(op, ast.LtE): return left <= right
        if isinstance(op, ast.Lt): return left < right
        if isinstance(op, ast.Eq): return left == right
        if isinstance(op, ast.NotEq): return left != right
        return None
    if isinstance(node, ast.IfExp):
        cond = _eval_expr_z3(node.test, env)
        t = _eval_expr_z3(node.body, env)
        e = _eval_expr_z3(node.orelse, env)
        if cond is not None and t is not None and e is not None:
            return Z3If(cond, t, e)
        return None
    if isinstance(node, ast.Call) and isinstance(node.func, ast.Name):
        if node.func.id == "abs" and len(node.args) == 1:
            arg = _eval_expr_z3(node.args[0], env)
            if arg is not None:
                return Z3If(arg >= 0, arg, -arg)
        if node.func.id == "max" and len(node.args) == 2:
            a = _eval_expr_z3(node.args[0], env)
            b = _eval_expr_z3(node.args[1], env)
            if a is not None and b is not None:
                return Z3If(a >= b, a, b)
        if node.func.id == "min" and len(node.args) == 2:
            a = _eval_expr_z3(node.args[0], env)
            b = _eval_expr_z3(node.args[1], env)
            if a is not None and b is not None:
                return Z3If(a <= b, a, b)
    return None


def _parse_spec_z3(spec_str: str, result_expr: Any, params: dict) -> Any:
    """Parse a spec string into a Z3 constraint using the result expression."""
    s = spec_str.strip().lower()

    # result >= N / result > N / result <= N / result < N
    m = re.match(r"^result\s*(>=|>|<=|<|==|!=)\s*(\d+)$", s)
    if m:
        op, val = m.group(1), int(m.group(2))
        if op == ">=": return result_expr >= val
        if op == ">": return result_expr > val
        if op == "<=": return result_expr <= val
        if op == "<": return result_expr < val
        if op == "==": return result_expr == val
        if op == "!=": return result_expr != val

    # result >= param / result > param etc.
    m = re.match(r"^result\s*(>=|>|<=|<|==|!=)\s*(\w+)$", s)
    if m:
        op, pname = m.group(1), m.group(2)
        if pname in params:
            p = params[pname]
            if op == ">=": return result_expr >= p
            if op == ">": return result_expr > p
            if op == "<=": return result_expr <= p
            if op == "<": return result_expr < p
            if op == "==": return result_expr == p
            if op == "!=": return result_expr != p

    # param > N
    m = re.match(r"^(\w+)\s*(>=|>|<=|<)\s*(\d+)$", s)
    if m:
        pname, op, val = m.group(1), m.group(2), int(m.group(3))
        if pname in params:
            p = params[pname]
            if op == ">=": return p >= val
            if op == ">": return p > val
            if op == "<=": return p <= val
            if op == "<": return p < val

    return None


def _extract_lambda_source(fn: Callable) -> str:
    """Try to recover the source text of a lambda."""
    try:
        src = inspect.getsource(fn).strip()
        # Extract the lambda body
        m = re.search(r"lambda\s+[^:]+:\s*(.+?)(?:\)|$)", src)
        if m:
            return m.group(1).strip().rstrip(",).:")
        return src
    except (OSError, TypeError):
        return repr(fn)


# ═══════════════════════════════════════════════════════════════════
#  Target resolution — function / module / filepath
# ═══════════════════════════════════════════════════════════════════

def _collect_decorated_functions(target: Any) -> list[Callable]:
    """Extract all functions with ``_deppy_spec`` from a target."""
    # Single function
    if callable(target) and not isinstance(target, type) and not isinstance(target, types.ModuleType):
        if hasattr(target, "_deppy_spec"):
            return [target]
        return []

    # Module
    if isinstance(target, types.ModuleType):
        fns = []
        for name in dir(target):
            obj = getattr(target, name)
            if callable(obj) and hasattr(obj, "_deppy_spec"):
                fns.append(obj)
        return fns

    return []


def _resolve_target(target: Any) -> tuple[list[Callable], str, str]:
    """Resolve target to (functions, module_name, source_file)."""
    # File path string
    if isinstance(target, str) and (target.endswith(".py") or os.path.isfile(target)):
        path = Path(target).resolve()
        module_name = path.stem
        spec = importlib.util.spec_from_file_location(module_name, str(path))
        if spec is None or spec.loader is None:
            raise ValueError(f"Cannot import {target}")
        mod = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(mod)
        fns = _collect_decorated_functions(mod)
        return fns, module_name, str(path)

    # Module
    if isinstance(target, types.ModuleType):
        module_name = getattr(target, "__name__", "Module")
        source_file = getattr(target, "__file__", "")
        fns = _collect_decorated_functions(target)
        return fns, module_name, source_file or ""

    # Single function
    if callable(target):
        fname = getattr(target, "__name__", "anon")
        module_name = fname.capitalize()
        source_file = ""
        try:
            source_file = inspect.getfile(target)
        except (TypeError, OSError):
            pass
        fns = [target] if hasattr(target, "_deppy_spec") else [target]
        return fns, module_name, source_file

    raise TypeError(f"compile_to_lean: unsupported target type {type(target)}")


# ═══════════════════════════════════════════════════════════════════
#  Trust level computation
# ═══════════════════════════════════════════════════════════════════

def _compute_trust_level(sorry_count: int, lean_text: str) -> str:
    """Determine the trust level for the certificate.

    - LEAN_VERIFIED:          0 sorry's and 0 axioms beyond Mathlib
    - LEAN_EXPORTED:          sorry's present
    - ASSUMPTION_DEPENDENT:   axiom declarations present
    """
    has_axiom = "axiom " in lean_text
    if sorry_count == 0 and not has_axiom:
        return "LEAN_VERIFIED"
    if has_axiom:
        return "ASSUMPTION_DEPENDENT"
    return "LEAN_EXPORTED"


# ═══════════════════════════════════════════════════════════════════
#  Main entry point
# ═══════════════════════════════════════════════════════════════════

def compile_to_lean(
    target: Union[Callable, types.ModuleType, str],
    output_path: str | Path | None = None,
) -> LeanCertificate:
    """Compile decorated Python functions to a complete Lean 4 file.

    Parameters
    ----------
    target
        A decorated function (has ``_deppy_spec``), a module (scanned for
        all decorated functions), or a file path string (imported and scanned).
    output_path
        If given, the rendered Lean file is written here.

    Returns
    -------
    LeanCertificate
        Contains rendered declarations, theorems, and metadata.
    """
    fns, module_name, source_file = _resolve_target(target)

    all_decls: list[str] = []
    all_thms: list[str] = []
    total_sorry = 0
    total_proved = 0
    all_obligations: list[str] = []

    for fn in fns:
        decls, thms, sorry_count, proved_count, obligations = _compile_function(fn)
        all_decls.extend(decls)
        all_thms.extend(thms)
        total_sorry += sorry_count
        total_proved += proved_count
        all_obligations.extend(obligations)

    # Combine all text for import inference
    combined = "\n".join(all_decls + all_thms)
    imports = _infer_imports(combined)

    # Compute trust level
    trust_level = _compute_trust_level(total_sorry, combined)

    cert = LeanCertificate(
        module_name=module_name,
        imports=imports,
        declarations=all_decls,
        theorems=all_thms,
        sorry_count=total_sorry,
        proved_count=total_proved,
        trust_level=trust_level,
        source_file=source_file,
        obligations=all_obligations,
    )

    if output_path is not None:
        out = Path(output_path)
        out.parent.mkdir(parents=True, exist_ok=True)
        out.write_text(cert.render(), encoding="utf-8")

    return cert
