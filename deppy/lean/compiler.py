_SPEC_PATTERNS: list[tuple[re.Pattern, Callable[..., str | None]]] = []


def _register_pattern(pattern: str):
    def decorator(fn):
        _SPEC_PATTERNS.append((re.compile(pattern, re.IGNORECASE), fn))
        return fn
    return decorator


@_register_pattern(r"result\s*>=?\s*(\d+)")
def _spec_result_ge(m, params, ret):
    val = m.group(1)
    return f"({val} ≤ {ret})" if ">=" in m.group(0) else f"({val} < {ret})"


@_register_pattern(r"result\s*<=?\s*(\d+)")
def _spec_result_le(m, params, ret):
    val = m.group(1)
    return f"({ret} ≤ {val})" if "<=" in m.group(0) else f"({ret} < {val})"


@_register_pattern(r"result\s*>\s*(\d+)")
def _spec_result_gt(m, params, ret):
    val = m.group(1)
    return f"({val} < {ret})"


@_register_pattern(r"result\s+is\s+sorted")
def _spec_sorted(m, params, ret):
    return f"List.Sorted (· ≤ ·) {ret}"


@_register_pattern(r"result\s+is\s+a\s+permutation\s+of\s+(\w+)")
def _spec_perm(m, params, ret):
    src = m.group(1)
    return f"List.Perm {src} {ret}"


@_register_pattern(r"(\w+)\s*>\s*(\d+)")
def _spec_var_gt(m, params, ret):
    var, val = m.group(1), m.group(2)
    if var in params:
        return f"({val} < {var})"
    return None


@_register_pattern(r"(\w+)\s*>=\s*(\d+)")
def _spec_var_ge(m, params, ret):
    var, val = m.group(1), m.group(2)
    if var in params:
        return f"({val} ≤ {var})"
    return None


def _fallback_translate_spec(spec_desc: str, param_names: list[str],
                             return_name: str = "result") -> str | None:
    """Pattern-based spec→Lean translation as a fallback."""
    for pat, handler in _SPEC_PATTERNS:
        m = pat.search(spec_desc)
        if m:
            result = handler(m, param_names, return_name)
            if result is not None:
                return result
    return None


