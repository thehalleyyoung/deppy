"""spec_inference.py — Formal C4 Spec Inference from Python Source

Generates FORMAL specifications in the C4 type theory language from
Python source code.  Every spec is a structured obligation:

    Π(args : {InputType | requires}).
      {result : OutputType | ensures}

where requires/ensures are Python boolean expressions (not English),
fibers are isinstance-dispatch cases with per-branch postconditions,
and the whole thing compiles to C4 verification conditions via the
c4_compiler.

Two inference backends:
  1. STATIC — AST analysis: type annotations, asserts, isinstance
     dispatch, return expressions, raise guards, __init__ invariants
  2. LLM — structured prompt asking for formal Python-expression
     pre/postconditions (the main path for real verification)

Specs are classified by quality:
  FORMAL   — has ≥1 non-trivial ensures/returns_expr (worth proving)
  PARTIAL  — has only shape specs (isinstance, type annotations)
  TRIVIAL  — no real spec extracted (proof is vacuous — honestly labeled)

SYNERGY INTEGRATION:
  - Fiber decomposition feeds directly into RefinementDescent (Synergy 1)
  - Product covers compose interprocedurally (Synergy 2)
  - Z3-decidable predicates get hypothesis pushing (Synergy 1)
  - Non-Z3 predicates marked LIBRARY/ORACLE decidability
"""
from __future__ import annotations

import ast
import re
import textwrap
from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Dict, FrozenSet, List, Optional, Set, Tuple

from cctt.proof_theory.predicates import Decidability


# ═══════════════════════════════════════════════════════════════════
# Spec Quality Classification
# ═══════════════════════════════════════════════════════════════════

class SpecSource(Enum):
    """How the spec was generated."""
    STATIC = "static"       # extracted from source code AST
    LLM = "llm"             # generated by LLM (formal Python exprs)
    MANUAL = "manual"       # hand-written by developer
    TRIVIAL = "trivial"     # placeholder — no real spec


class SpecStrength(Enum):
    """How useful is the spec for verification?"""
    FORMAL = "formal"       # ≥1 non-trivial ensures or exact returns_expr
    PARTIAL = "partial"     # only shape/type specs (isinstance, annotation)
    TRIVIAL = "trivial"     # nothing useful extracted


