# The system prompt instructs the LLM to produce formal C4 specs
# as Python boolean expressions, NOT English descriptions.

LLM_SPEC_SYSTEM_PROMPT = """\
You are a formal specification writer for C⁴ (Cubical Cohomological Calculus \
of Constructions). Given Python source code, generate FORMAL specifications \
as Python boolean expressions.

OUTPUT FORMAT (JSON):
{
  "requires": ["isinstance(x, int)", "x >= 0"],
  "ensures": [
    "isinstance(result, list)",
    "len(result) == len(input)",
    "all(result[i] <= result[i+1] for i in range(len(result)-1))"
  ],
  "returns_expr": "sorted(input)",
  "fibers": [
    {"name": "empty", "guard": "len(input) == 0", "ensures": ["result == []"]},
    {"name": "singleton", "guard": "len(input) == 1", "ensures": ["result == input"]},
    {"name": "general", "guard": "len(input) > 1",
     "ensures": ["len(result) == len(input)", "result[0] <= result[-1]"]}
  ],
  "invariants": [],
  "pure": true
}

RULES:
1. Every expression MUST be a valid Python boolean expression
2. Use `result` to refer to the return value
3. Use parameter names from the function signature
4. For methods, `self` refers to the instance
5. NEVER write English descriptions — only Python expressions
6. Fibers MUST have exhaustive guards (∨ guard_i covers all inputs)
7. Be as PRECISE as possible:
   - Prefer `result == sorted(input)` over `isinstance(result, list)`
   - Prefer `len(result) == len(input)` over `result is not None`
   - Prefer exact returns_expr when the function is simple
8. For complex functions, use fiber decomposition:
   - Each isinstance/if branch becomes a fiber
   - Per-fiber postconditions can be stronger than global ones
9. Mark purity: True if no side effects, False if mutates state
10. For classes: invariants are predicates preserved by all methods

EXAMPLES OF GOOD SPECS:

# abs(x)
{"requires": ["isinstance(x, (int, float))"],
 "ensures": ["result >= 0", "result == x or result == -x"],
 "returns_expr": "x if x >= 0 else -x",
 "fibers": [
   {"name": "non_negative", "guard": "x >= 0", "ensures": ["result == x"]},
   {"name": "negative", "guard": "x < 0", "ensures": ["result == -x"]}
 ], "pure": true}

# list.append(self, item) 
{"requires": [],
 "ensures": ["len(self) == len(old_self) + 1", "self[-1] == item"],
 "fibers": [], "pure": false}

EXAMPLES OF BAD SPECS (DO NOT DO THIS):
- "ensures": ["behaves correctly"]  ← English, not Python
- "ensures": ["returns the expected output"]  ← English
- "returns_expr": "the sorted version"  ← English
"""

LLM_SPEC_USER_TEMPLATE = """\
Generate a formal C4 spec for this Python function.

```python
{source}
```

Qualified name: {qualname}
Library: {library_name}
{docstring_section}

Respond with ONLY the JSON spec object. No explanation, no markdown fences.
"""


def build_llm_spec_prompt(source: str, qualname: str,
                           library_name: str, docstring: str = "") -> Tuple[str, str]:
    """Build (system_prompt, user_prompt) for LLM spec generation.

    Returns a tuple of (system, user) messages ready for any LLM API.
    """
    doc_section = ""
    if docstring:
        doc_section = f"Docstring:\n{docstring}\n"
    user = LLM_SPEC_USER_TEMPLATE.format(
        source=source.strip(),
        qualname=qualname,
        library_name=library_name,
        docstring_section=doc_section,
    )
    return LLM_SPEC_SYSTEM_PROMPT, user


def parse_llm_spec_response(response: str) -> C4Spec:
    """Parse an LLM response into a C4Spec.

    Handles JSON extraction from potentially noisy LLM output.
    """
    import json

    # Strip markdown fences if present
    text = response.strip()
    if text.startswith("```"):
        lines = text.split("\n")
        lines = [l for l in lines if not l.startswith("```")]
        text = "\n".join(lines)

    # Find JSON object
    start = text.find("{")
    end = text.rfind("}") + 1
    if start < 0 or end <= start:
        return C4Spec(source=SpecSource.LLM, strength=SpecStrength.TRIVIAL)

    try:
        d = json.loads(text[start:end])
    except json.JSONDecodeError:
        return C4Spec(source=SpecSource.LLM, strength=SpecStrength.TRIVIAL)

    spec = C4Spec(
        requires=d.get("requires", []),
        ensures=d.get("ensures", []),
        returns_expr=d.get("returns_expr"),
        fibers=[FiberClause.from_json(f) for f in d.get("fibers", [])],
        invariants=d.get("invariants", []),
        purity=d.get("pure"),
        source=SpecSource.LLM,
    )
    spec.strength = spec.classify_strength()
    return spec


