# Top-level API
# ═══════════════════════════════════════════════════════════════════

_ANALYZER = StaticSpecAnalyzer()


def infer_c4_spec(source: str, name: str, qualname: str = "",
                   params: Optional[List[str]] = None,
                   docstring: str = "",
                   llm_response: Optional[str] = None) -> C4Spec:
    """Infer a formal C4 spec for a Python function.

    Strategy:
    1. If llm_response is provided, parse it (LLM path)
    2. Otherwise, run static analysis
    3. Classify strength honestly

    The LLM path is the main path for real verification.
    Static analysis is the honest fallback that produces what it can.

    Args:
        source: Function source code
        name: Function name
        qualname: Fully qualified name
        params: Parameter names
        docstring: Docstring
        llm_response: Pre-computed LLM response (if available)

    Returns:
        C4Spec with formal predicates and honest strength classification
    """
    # LLM path — the main game
    if llm_response is not None:
        llm_spec = parse_llm_spec_response(llm_response)
        if not llm_spec.is_trivial:
            return llm_spec

    # Static fallback
    static_spec = _ANALYZER.infer(source, name, params, qualname, docstring)
    return static_spec
