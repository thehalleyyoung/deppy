# C4 Spec — the formal obligation IR
# ═══════════════════════════════════════════════════════════════════

@dataclass
class FiberClause:
    """A case in the fiber decomposition (Čech cover).

    Each fiber is a face of the cubical proof: restricting the function
    to inputs satisfying `guard`, the postconditions `ensures` hold.

    In C4: RefinementFiber(name, guard) with per-fiber PathSpec.
    """
    name: str               # e.g. "positive", "negative", "empty"
    guard: str              # Python bool expression: "x > 0", "isinstance(x, int)"
    ensures: List[str]      # postconditions under this guard
    returns_expr: Optional[str] = None  # exact return under this guard
    decidability: str = "unknown"       # z3/structural/library/oracle

    def to_json(self) -> Dict[str, Any]:
        d: Dict[str, Any] = {
            "name": self.name, "guard": self.guard,
            "ensures": self.ensures, "decidability": self.decidability,
        }
        if self.returns_expr:
            d["returns_expr"] = self.returns_expr
        return d

    @staticmethod
    def from_json(d: Dict[str, Any]) -> FiberClause:
        return FiberClause(
            name=d.get("name", ""),
            guard=d.get("guard", "True"),
            ensures=d.get("ensures", []),
            returns_expr=d.get("returns_expr"),
            decidability=d.get("decidability", "unknown"),
        )


@dataclass
class C4Spec:
    """A formal specification in C4 type theory language.

    The spec is a dependent function type with refinement:

        Π(args : {InputType | ∧ requires}).
          {result : OutputType | ∧ ensures}

    When fibers are present, the spec decomposes:

        Π(args : {InputType | guard_i}).
          {result : OutputType | ensures_i}    for each fiber i

    with exhaustiveness: ∨ guard_i ⊇ input domain
    and overlap compatibility: guard_i ∧ guard_j ⟹ ensures_i ↔ ensures_j

    FIELDS:
      requires   — preconditions as Python bool expressions over params
      ensures    — postconditions as Python bool expressions over `result`
      returns_expr — exact return expression (strongest: f(x) ≡ expr)
      fibers     — case analysis with per-case ensures
      invariants — class invariants (for classes)
      purity     — whether the function is pure
      source     — how the spec was generated
      strength   — how useful the spec is

    SYNERGY NOTES:
      - Each `ensures` compiles to a VC via Z3Env
      - Each fiber compiles to a RefinementFiber for the C4 compiler
      - `requires` become the refinement predicate on the input type
      - `returns_expr` is the strongest spec: Path(f(x), returns_expr)
      - Invariants become InvariantSpec objects for descent proofs

    EFFECT-AWARE SPECS:
      For impure functions, the spec becomes a state contract:
        effects     — FunctionEffect describing what the function does to state
        state_contract — pre/post state spec with modifies/old() bindings
      Pure functions have effects=None (equational reasoning is valid).
      Impure functions need pre/post state reasoning (like Dafny/F*).
    """
    requires: List[str] = field(default_factory=list)
    ensures: List[str] = field(default_factory=list)
    returns_expr: Optional[str] = None
    fibers: List[FiberClause] = field(default_factory=list)
    invariants: List[str] = field(default_factory=list)
    purity: Optional[bool] = None
    source: SpecSource = SpecSource.TRIVIAL
    strength: SpecStrength = SpecStrength.TRIVIAL
    # Effect model — what does this function DO to state?
    effects: Optional[Any] = None        # FunctionEffect (avoid circular import at type level)
    state_contract: Optional[Any] = None  # StateContract (pre/post state specs)

    def classify_strength(self) -> SpecStrength:
        """Compute strength from content.

        FORMAL: has exact returns_expr, relational ensures (==, >=, <=),
                or fiber decomposition with per-case returns.
        PARTIAL: only shape specs (isinstance, type annotations).
        TRIVIAL: nothing useful extracted.
        """
        if self.returns_expr:
            return SpecStrength.FORMAL
        # Non-trivial ensures (beyond isinstance/type checks)
        for e in self.ensures:
            if not e.startswith("isinstance(result,"):
                return SpecStrength.FORMAL
        # Fibers with per-case returns_expr or non-trivial ensures
        for f in self.fibers:
            if f.returns_expr:
                return SpecStrength.FORMAL
            for e in f.ensures:
                if not e.startswith("isinstance(result,"):
                    return SpecStrength.FORMAL
        # Only shape specs
        if self.ensures or self.fibers or self.invariants:
            return SpecStrength.PARTIAL
        return SpecStrength.TRIVIAL

    @property
    def is_trivial(self) -> bool:
        return self.classify_strength() == SpecStrength.TRIVIAL

    @property
    def is_formal(self) -> bool:
        return self.classify_strength() == SpecStrength.FORMAL

    def path_rhs(self, func_name: str, params: List[str]) -> str:
        """Compile to a formal RHS for a PathSpec.

        Priority: returns_expr > ensures conjunction > <unspecified>.
        """
        if self.returns_expr:
            return self.returns_expr
        if self.ensures:
            return " and ".join(self.ensures)
        return f"<unspecified:{func_name}>"

    def input_predicate(self) -> str:
        """Compile requires to a single predicate string."""
        if not self.requires:
            return "True"
        return " and ".join(self.requires)

    def output_predicate(self) -> str:
        """Compile ensures to a single predicate string."""
        if not self.ensures:
            return "True"
        return " and ".join(self.ensures)

    def to_json(self) -> Dict[str, Any]:
        d: Dict[str, Any] = {
            "source": self.source.value,
            "strength": self.classify_strength().value,
        }
        if self.requires:
            d["requires"] = self.requires
        if self.ensures:
            d["ensures"] = self.ensures
        if self.returns_expr:
            d["returns_expr"] = self.returns_expr
        if self.fibers:
            d["fibers"] = [f.to_json() for f in self.fibers]
        if self.invariants:
            d["invariants"] = self.invariants
        if self.purity is not None:
            d["pure"] = self.purity
        if self.effects is not None:
            d["effects"] = self.effects.to_json()
        if self.state_contract is not None:
            d["state_contract"] = self.state_contract.to_json()
        return d

    @staticmethod
    def from_json(d: Dict[str, Any]) -> C4Spec:
        src = d.get("source", "trivial")
        effects = None
        state_contract = None
        if "effects" in d:
            from cctt.proof_theory.effect_model import FunctionEffect
            effects = FunctionEffect.from_json(d["effects"])
        if "state_contract" in d:
            from cctt.proof_theory.effect_model import StateContract
            state_contract = StateContract.from_json(d["state_contract"])
        return C4Spec(
            requires=d.get("requires", []),
            ensures=d.get("ensures", []),
            returns_expr=d.get("returns_expr"),
            fibers=[FiberClause.from_json(f) for f in d.get("fibers", [])],
            invariants=d.get("invariants", []),
            purity=d.get("pure"),
            source=SpecSource(src) if src in [e.value for e in SpecSource] else SpecSource.TRIVIAL,
            effects=effects,
            state_contract=state_contract,
        )


