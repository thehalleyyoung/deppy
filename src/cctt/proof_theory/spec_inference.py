# Static Spec Analyzer — extract formal specs from Python AST
# ═══════════════════════════════════════════════════════════════════

class StaticSpecAnalyzer:
    """Extract formal C4 specs from Python source code via AST analysis.

    Extraction rules (all produce Python boolean expressions, not English):

    1. Return type annotation  → ensures isinstance(result, T)
    2. Parameter annotations   → requires isinstance(p, T)
    3. Assert statements       → requires (early) or ensures (late)
    4. isinstance if/elif      → fiber decomposition with per-branch returns
    5. Conditional returns     → fibers with guard → returns_expr
    6. Single return expr      → returns_expr (exact return spec)
    7. raise TypeError/ValueError → requires (negated guard at entry)
    8. self.attr = in __init__ → invariant hasattr(self, 'attr')
    9. Attribute access on params → requires hasattr(param, attr)
    10. Pure detection         → no global/nonlocal, no self mutation
    """

    def infer(self, source: str, name: str, params: Optional[List[str]] = None,
              qualname: str = "", docstring: str = "") -> C4Spec:
        """Infer a C4Spec from Python source code.

        Args:
            source: The function/class source code
            name: Function/class name
            params: Parameter names (if known)
            qualname: Fully qualified name
            docstring: Docstring (used only as fallback hint)

        Returns:
            C4Spec with formal predicates extracted from AST
        """
        try:
            tree = ast.parse(textwrap.dedent(source))
        except SyntaxError:
            return C4Spec(source=SpecSource.STATIC)

        fn_node = self._find_function(tree, name)
        if fn_node is None:
            cls_node = self._find_class(tree, name)
            if cls_node is not None:
                return self._infer_class(cls_node, name, qualname)
            return C4Spec(source=SpecSource.STATIC)

        requires: List[str] = []
        ensures: List[str] = []
        fibers: List[FiberClause] = []
        returns_expr: Optional[str] = None
        purity: Optional[bool] = None

        actual_params = params or self._extract_params(fn_node)

        # 1. Return type annotation → ensures
        ret_ensures = self._extract_return_type_ensures(fn_node)
        if ret_ensures:
            ensures.extend(ret_ensures)

        # 2. Parameter annotations → requires
        param_requires = self._extract_param_requires(fn_node)
        requires.extend(param_requires)

        # 3. Assert statements → requires/ensures
        assert_req, assert_ens = self._extract_asserts(fn_node, actual_params)
        requires.extend(assert_req)
        ensures.extend(assert_ens)

        # 4+5. Conditional dispatch → fiber decomposition
        extracted_fibers = self._extract_fibers(fn_node, actual_params)
        fibers.extend(extracted_fibers)

        # 6. Single/simple return → returns_expr
        if not fibers:
            returns_expr = self._extract_single_return(fn_node, actual_params)

        # 7. Raise at entry → requires (negated guard)
        raise_requires = self._extract_raise_requires(fn_node)
        requires.extend(raise_requires)

        # 9. Attribute access → requires hasattr
        attr_requires = self._extract_attr_requires(fn_node, actual_params)
        requires.extend(attr_requires)

        # 10. Purity detection (legacy — now superseded by effect analysis)
        purity = self._detect_purity(fn_node)

        # 10b. Full effect analysis — Python-specific mutation/IO/nondeterminism
        from cctt.proof_theory.effect_model import (
            EffectAnalyzer, FunctionEffect, StateContract,
            synthesize_state_contract,
        )
        effect_analyzer = EffectAnalyzer()
        effects = effect_analyzer.analyze_node(fn_node)
        state_contract = synthesize_state_contract(effects, name, actual_params)

        # Override purity from effect analysis (more precise)
        purity = effects.is_pure

        # Merge state contract pre/post into requires/ensures
        for pre in state_contract.pre_requires:
            if pre not in requires:
                requires.append(pre)
        for post in state_contract.post_ensures:
            if post not in ensures:
                ensures.append(post)

        # 11. Synthesize global postconditions from fiber analysis
        fiber_ensures = self._synthesize_global_postconditions(fibers, ensures)
        ensures.extend(fiber_ensures)

        # Deduplicate
        requires = list(dict.fromkeys(requires))
        ensures = list(dict.fromkeys(ensures))

        spec = C4Spec(
            requires=requires,
            ensures=ensures,
            returns_expr=returns_expr,
            fibers=fibers,
            purity=purity,
            source=SpecSource.STATIC,
            effects=effects if not effects.is_pure else None,
            state_contract=state_contract if not state_contract.is_trivial else None,
        )
        spec.strength = spec.classify_strength()
        return spec

    # ── AST navigation helpers ───────────────────────────────────

    def _find_function(self, tree: ast.Module, name: str) -> Optional[ast.FunctionDef]:
        for node in ast.walk(tree):
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                if node.name == name:
                    return node
        return None

    def _find_class(self, tree: ast.Module, name: str) -> Optional[ast.ClassDef]:
        for node in ast.walk(tree):
            if isinstance(node, ast.ClassDef) and node.name == name:
                return node
        return None

    def _extract_params(self, fn: ast.FunctionDef) -> List[str]:
        params = []
        for arg in fn.args.args:
            if arg.arg != 'self' and arg.arg != 'cls':
                params.append(arg.arg)
        return params

    # ── Rule 1: Return type annotation ───────────────────────────

    def _extract_return_type_ensures(self, fn: ast.FunctionDef) -> List[str]:
        if fn.returns is None:
            return []
        try:
            ret_str = ast.unparse(fn.returns)
        except Exception:
            return []
        # Skip trivially unhelpful annotations
        if ret_str in ("Any", "None", "object"):
            return []
        # Optional[X] → isinstance(result, X) when result is not None
        m = re.match(r'^Optional\[(.+)\]$', ret_str)
        if m:
            return [f"result is None or isinstance(result, {m.group(1)})"]
        # Union[X, Y] → isinstance(result, (X, Y))
        m = re.match(r'^Union\[(.+)\]$', ret_str)
        if m:
            types = m.group(1)
            return [f"isinstance(result, ({types}))"]
        # Tuple[X, Y] → isinstance(result, tuple) and len(result) == N
        m = re.match(r'^[Tt]uple\[(.+)\]$', ret_str)
        if m:
            inner = m.group(1)
            n = len([x.strip() for x in inner.split(',') if x.strip() != '...'])
            if '...' not in inner:
                return [f"isinstance(result, tuple)", f"len(result) == {n}"]
            return [f"isinstance(result, tuple)"]
        # List[X] → isinstance(result, list)
        if ret_str.startswith(("List[", "list[")):
            inner = ret_str.split("[", 1)[1].rstrip("]")
            return [f"isinstance(result, list)",
                    f"all(isinstance(x, {inner}) for x in result)"]
        # Dict[K, V]
        if ret_str.startswith(("Dict[", "dict[")):
            return [f"isinstance(result, dict)"]
        # Set[X]
        if ret_str.startswith(("Set[", "set[")):
            return [f"isinstance(result, set)"]
        # bool
        if ret_str == "bool":
            return [f"isinstance(result, bool)"]
        # str, int, float
        if ret_str in ("str", "int", "float"):
            return [f"isinstance(result, {ret_str})"]
        # General type
        if ret_str[0].isupper() or '.' in ret_str:
            return [f"isinstance(result, {ret_str})"]
        return []

    # ── Rule 2: Parameter type annotations ───────────────────────

    def _extract_param_requires(self, fn: ast.FunctionDef) -> List[str]:
        requires = []
        for arg in fn.args.args:
            if arg.arg in ('self', 'cls'):
                continue
            if arg.annotation is not None:
                try:
                    ann_str = ast.unparse(arg.annotation)
                except Exception:
                    continue
                if ann_str in ("Any", "object"):
                    continue
                requires.append(f"isinstance({arg.arg}, {ann_str})")
        return requires

    # ── Rule 3: Assert statements ────────────────────────────────

    def _extract_asserts(self, fn: ast.FunctionDef,
                         params: List[str]) -> Tuple[List[str], List[str]]:
        """Extract assert statements as requires (if referencing params) or ensures."""
        requires = []
        ensures = []
        body = fn.body
        # Skip docstring
        start = 0
        if body and isinstance(body[0], ast.Expr) and isinstance(body[0].value, (ast.Constant,)):
            start = 1

        for i, stmt in enumerate(body[start:], start):
            if not isinstance(stmt, ast.Assert):
                continue
            try:
                expr_str = ast.unparse(stmt.test)
            except Exception:
                continue
            # Check if it references only params (→ requires) or result-like vars
            names = {n.id for n in ast.walk(stmt.test) if isinstance(n, ast.Name)}
            param_set = set(params) | {'self', 'cls'}
            if names <= param_set:
                requires.append(expr_str)
            else:
                ensures.append(expr_str)
        return requires, ensures

    # ── Rules 4+5: Fiber decomposition from branches ─────────────

    def _extract_fibers(self, fn: ast.FunctionDef,
                        params: List[str]) -> List[FiberClause]:
        """Extract fiber decomposition from if/elif/else chains.

        Patterns detected:
        - isinstance dispatch: if isinstance(x, T): return ...
        - Value dispatch: if x > 0: return ... elif x < 0: return ...
        - Type dispatch: if type(x) == T: return ...
        """
        fibers: List[FiberClause] = []
        body = fn.body
        # Skip docstring
        start = 0
        if body and isinstance(body[0], ast.Expr) and isinstance(body[0].value, (ast.Constant,)):
            start = 1

        for stmt in body[start:]:
            if isinstance(stmt, ast.If):
                self._extract_if_chain_fibers(stmt, fibers, params)
                break  # Only extract the first top-level if-chain

        return fibers

    def _extract_if_chain_fibers(self, node: ast.If,
                                  fibers: List[FiberClause],
                                  params: List[str]) -> None:
        """Walk an if/elif/else chain extracting fibers."""
        chain = []
        current: Optional[ast.If] = node
        while current is not None:
            try:
                guard = ast.unparse(current.test)
            except Exception:
                guard = "<unparseable>"
            ret_expr = self._find_return_in_block(current.body)
            branch_ensures = []
            if ret_expr:
                branch_ensures.append(f"result == {ret_expr}")
            chain.append((guard, branch_ensures, ret_expr))

            # Check for elif (else containing a single If)
            if current.orelse and len(current.orelse) == 1 and isinstance(current.orelse[0], ast.If):
                current = current.orelse[0]
            else:
                # else branch
                if current.orelse:
                    ret_expr = self._find_return_in_block(current.orelse)
                    else_ensures = []
                    if ret_expr:
                        else_ensures.append(f"result == {ret_expr}")
                    negated = " and ".join(f"not ({g})" for g, _, _ in chain)
                    chain.append((negated, else_ensures, ret_expr))
                current = None

        # Only produce fibers if we found returns in branches
        if len(chain) >= 2 and any(ens for _, ens, _ in chain):
            for i, (guard, ens, ret) in enumerate(chain):
                name = self._fiber_name_from_guard(guard, i)
                decidability = self._classify_guard(guard)
                fibers.append(FiberClause(
                    name=name, guard=guard, ensures=ens,
                    returns_expr=ret, decidability=decidability,
                ))

    def _find_return_in_block(self, stmts: List[ast.stmt]) -> Optional[str]:
        """Find a direct return expression in a block of statements."""
        for stmt in stmts:
            if isinstance(stmt, ast.Return) and stmt.value is not None:
                try:
                    return ast.unparse(stmt.value)
                except Exception:
                    return None
        return None

    def _fiber_name_from_guard(self, guard: str, index: int) -> str:
        """Generate a readable fiber name from the guard expression."""
        # isinstance(x, Foo) → "Foo"
        m = re.search(r'isinstance\(\w+,\s*(\w+)\)', guard)
        if m:
            return m.group(1)
        # type(x) == Foo → "Foo"
        m = re.search(r'type\(\w+\)\s*==\s*(\w+)', guard)
        if m:
            return m.group(1)
        # x > 0 → "positive", x < 0 → "negative", x == 0 → "zero"
        if '> 0' in guard or '>= 1' in guard:
            return 'positive'
        if '< 0' in guard or '<= -1' in guard:
            return 'negative'
        if '== 0' in guard or 'is None' in guard:
            return 'zero_or_none'
        if 'not ' in guard and index == len(guard) - 1:
            return 'default'
        return f'case_{index}'

    def _classify_guard(self, guard: str) -> str:
        """Classify the decidability of a guard expression."""
        if 'isinstance' in guard or 'is None' in guard or 'hasattr' in guard:
            return "structural"
        # Arithmetic comparisons → Z3
        if re.search(r'[<>=!]+\s*\d', guard) or re.search(r'\d\s*[<>=!]+', guard):
            return "z3"
        if any(op in guard for op in ('>', '<', '>=', '<=', '==', '!=')):
            return "z3"
        return "library"

    # ── Rule 11: Global postcondition synthesis from fibers ───────

    def _synthesize_global_postconditions(
        self,
        fibers: List[FiberClause],
        existing_ensures: List[str],
    ) -> List[str]:
        """Derive global postconditions that hold across all fibers.

        A spec is *thorough* only when ensures uniquely determine the
        input→output relation.  Type-level ensures (isinstance) are
        necessary but never sufficient.  This method bridges the gap
        by lifting per-fiber returns_expr / ensures into global
        relational postconditions.

        Derivation rules:
        1. VALUE DISJUNCTION: If every fiber has returns_expr,
           ensures "result in {expr₁, expr₂, ...}" (up to 4 arms).
           When all arms are param expressions this uniquely pins f.

        2. NON-NEGATIVITY: If every fiber's returns_expr is provably
           ≥ 0 (literal positive, abs(), len(), negation of a negation,
           or param under x >= 0 guard), emit "result >= 0".

        3. BOUNDS: If fibers collectively show result in a bounded set
           (e.g. result ∈ {x, -x}), emit the bound.

        4. CONDITIONAL EQUALITY: Always emit the full conditional:
           "result == (expr₁ if guard₁ else expr₂ if guard₂ else ...)"
           This IS the definitional spec.

        5. FIBER-COMMON ENSURES: If every fiber has the same ensures
           predicate, lift it to global.
        """
        synthesized: List[str] = []
        existing_set = set(existing_ensures)

        if not fibers:
            return synthesized

        all_have_returns = all(f.returns_expr for f in fibers)

        # Rule 4: Conditional equality (definitional spec)
        if all_have_returns and 2 <= len(fibers) <= 6:
            cond = self._build_conditional_expr(fibers)
            if cond and cond not in existing_set:
                synthesized.append(f"result == ({cond})")

        # Rule 1: Value disjunction
        if all_have_returns and 2 <= len(fibers) <= 4:
            exprs = [f.returns_expr for f in fibers]
            unique = list(dict.fromkeys(exprs))
            if len(unique) >= 2:
                disj = " or ".join(f"result == {e}" for e in unique)
                if disj not in existing_set:
                    synthesized.append(disj)

        # Rule 2: Non-negativity
        if all_have_returns and self._all_nonneg(fibers):
            pred = "result >= 0"
            if pred not in existing_set:
                synthesized.append(pred)

        # Rule 5: Common ensures across all fibers
        if len(fibers) >= 2:
            common = self._common_fiber_ensures(fibers)
            for e in common:
                if e not in existing_set:
                    synthesized.append(e)

        return synthesized

    def _build_conditional_expr(self, fibers: List[FiberClause]) -> Optional[str]:
        """Build a conditional expression from fiber guards + returns.

        Produces: "expr₁ if guard₁ else expr₂ if guard₂ else expr₃"
        """
        parts: List[str] = []
        for i, f in enumerate(fibers):
            if not f.returns_expr:
                return None
            if i < len(fibers) - 1:
                parts.append(f"{f.returns_expr} if {f.guard}")
            else:
                parts.append(f.returns_expr)
        if not parts:
            return None
        result = " else ".join(parts)
        if len(result) > 300:
            return None
        return result

    def _all_nonneg(self, fibers: List[FiberClause]) -> bool:
        """Check if all fiber returns are provably non-negative."""
        for f in fibers:
            if not f.returns_expr:
                return False
            if not self._is_nonneg_expr(f.returns_expr, f.guard):
                return False
        return True

    def _is_nonneg_expr(self, expr: str, guard: str) -> bool:
        """Heuristic: is this expression non-negative under the guard?"""
        e = expr.strip()
        # Literal non-negative numbers
        try:
            val = float(e)
            return val >= 0
        except (ValueError, OverflowError):
            pass
        # abs(...)
        if e.startswith("abs("):
            return True
        # len(...)
        if e.startswith("len("):
            return True
        # Simple variable under guard that implies var >= 0
        if re.match(r'^[a-zA-Z_]\w*$', e):
            if re.search(rf'\b{re.escape(e)}\s*>=?\s*0', guard):
                return True
        # -var: non-negative when var <= 0
        if e.startswith("-") and re.match(r'^[a-zA-Z_]\w*$', e[1:].strip()):
            inner = e[1:].strip()
            # Explicit: "inner < 0" or "inner <= 0"
            if re.search(rf'\b{re.escape(inner)}\s*<=?\s*0', guard):
                return True
            if re.search(rf'\b{re.escape(inner)}\s*<\s*0', guard):
                return True
            # Negated form: "not (inner >= 0)" means inner < 0
            if re.search(rf'not\s*\(\s*{re.escape(inner)}\s*>=\s*0\s*\)', guard):
                return True
            # Negated form: "not (inner > 0)" means inner <= 0
            if re.search(rf'not\s*\(\s*{re.escape(inner)}\s*>\s*0\s*\)', guard):
                return True
        return False

    def _common_fiber_ensures(self, fibers: List[FiberClause]) -> List[str]:
        """Find ensures predicates common to ALL fibers."""
        if not fibers:
            return []
        common: Optional[Set[str]] = None
        for f in fibers:
            s = set(f.ensures)
            if common is None:
                common = s
            else:
                common = common & s
        return sorted(common) if common else []

    # ── Rule 6: Single return expression ─────────────────────────

    def _extract_single_return(self, fn: ast.FunctionDef,
                                params: List[str]) -> Optional[str]:
        """Extract the return expression if there's exactly one simple return."""
        returns: List[ast.Return] = []
        for node in ast.walk(fn):
            if isinstance(node, ast.Return) and node.value is not None:
                returns.append(node)

        if len(returns) != 1:
            return None

        ret = returns[0]
        try:
            expr_str = ast.unparse(ret.value)
        except Exception:
            return None

        # Only use if it's a "simple" expression (not too long, no side effects)
        if len(expr_str) > 200:
            return None
        # Skip if it's just returning a variable that's computed via complex logic
        # (we only want direct computations)
        if isinstance(ret.value, ast.Name):
            # Returning a single variable — too opaque to be useful as spec
            return None

        return expr_str

    # ── Rule 7: Raise at entry → requires ────────────────────────

    def _extract_raise_requires(self, fn: ast.FunctionDef) -> List[str]:
        """Extract preconditions from early raise patterns.

        Pattern: if not P(x): raise ValueError(...)  →  requires P(x)
        Only at function entry (first few statements).
        """
        requires = []
        body = fn.body
        start = 0
        if body and isinstance(body[0], ast.Expr) and isinstance(body[0].value, (ast.Constant,)):
            start = 1

        for stmt in body[start:start + 5]:  # only first 5 stmts
            if not isinstance(stmt, ast.If):
                continue
            # Check if the body is just a raise
            if not (len(stmt.body) == 1 and isinstance(stmt.body[0], ast.Raise)):
                continue
            # The guard is the BAD condition; negate it for requires
            try:
                guard = ast.unparse(stmt.test)
            except Exception:
                continue
            # if not P: raise → requires P
            if isinstance(stmt.test, ast.UnaryOp) and isinstance(stmt.test.op, ast.Not):
                try:
                    pos = ast.unparse(stmt.test.operand)
                    requires.append(pos)
                except Exception:
                    pass
            else:
                requires.append(f"not ({guard})")
        return requires

    # ── Rule 8: __init__ invariants ──────────────────────────────

    def _extract_init_invariants(self, fn: ast.FunctionDef) -> List[str]:
        """Extract class invariants from self.attr = ... in __init__."""
        invariants = []
        for node in ast.walk(fn):
            if isinstance(node, ast.Assign):
                for target in node.targets:
                    if (isinstance(target, ast.Attribute) and
                            isinstance(target.value, ast.Name) and
                            target.value.id == 'self'):
                        invariants.append(f"hasattr(self, '{target.attr}')")
        return list(dict.fromkeys(invariants))

    # ── Rule 9: Attribute access on params → requires ────────────

    def _extract_attr_requires(self, fn: ast.FunctionDef,
                                params: List[str]) -> List[str]:
        """Extract hasattr requirements from parameter attribute access.

        Only for first-level attribute access on function parameters.
        Skip 'self', 'cls', and common universal attrs.
        """
        requires = []
        skip_attrs = {
            '__class__', '__dict__', '__doc__', '__init__', '__module__',
            '__name__', '__repr__', '__str__', '__hash__', '__eq__',
        }
        seen: Set[Tuple[str, str]] = set()
        for node in ast.walk(fn):
            if (isinstance(node, ast.Attribute) and
                    isinstance(node.value, ast.Name) and
                    node.value.id in params and
                    node.value.id not in ('self', 'cls') and
                    node.attr not in skip_attrs):
                key = (node.value.id, node.attr)
                if key not in seen:
                    seen.add(key)
                    requires.append(f"hasattr({node.value.id}, '{node.attr}')")
        return requires

    # ── Rule 10: Purity detection ────────────────────────────────

    def _detect_purity(self, fn: ast.FunctionDef) -> Optional[bool]:
        """Detect if a function is pure (no side effects).

        Pure if: no global/nonlocal, no self.attr assignment (except __init__),
        no print/logging calls, no file I/O.
        """
        for node in ast.walk(fn):
            if isinstance(node, (ast.Global, ast.Nonlocal)):
                return False
            if isinstance(node, ast.Assign):
                for target in node.targets:
                    if isinstance(target, ast.Attribute):
                        if fn.name != '__init__':
                            return False
            if isinstance(node, ast.AugAssign):
                if isinstance(node.target, ast.Attribute):
                    return False
            if isinstance(node, ast.Call):
                if isinstance(node.func, ast.Name):
                    if node.func.id in ('print', 'open', 'write'):
                        return False
                if isinstance(node.func, ast.Attribute):
                    if node.func.attr in ('write', 'append', 'extend',
                                          'insert', 'remove', 'pop',
                                          'update', 'clear', 'setdefault'):
                        # Could be mutating — only impure if on self
                        if (isinstance(node.func.value, ast.Name) and
                                node.func.value.id == 'self'):
                            if fn.name != '__init__':
                                return False
        return True

    # ── Class inference ──────────────────────────────────────────

    def _infer_class(self, cls_node: ast.ClassDef, name: str,
                     qualname: str) -> C4Spec:
        """Infer spec for a class: invariants from __init__, method dispatch."""
        invariants: List[str] = []
        ensures: List[str] = []

        # Find __init__ and extract invariants
        for node in cls_node.body:
            if isinstance(node, ast.FunctionDef) and node.name == '__init__':
                invariants = self._extract_init_invariants(node)
                break

        # Base classes → ensures
        for base in cls_node.bases:
            try:
                base_name = ast.unparse(base)
                if base_name not in ('object',):
                    ensures.append(f"isinstance(self, {base_name})")
            except Exception:
                pass

        return C4Spec(
            ensures=ensures,
            invariants=invariants,
            source=SpecSource.STATIC,
            strength=SpecStrength.PARTIAL if invariants else SpecStrength.TRIVIAL,
        )


