"""Lean 4 artifact emitter for hybrid claims and obligations."""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Any, Dict, Iterable, List, Optional, Sequence


@dataclass(frozen=True)
class LeanClaim:
    """A claim to emit as a Lean theorem."""

    name: str
    proposition: str
    assumptions: Sequence[str] = field(default_factory=tuple)
    proof_hint: str = ""
    source: str = ""
    trust_level: str = "UNTRUSTED"


@dataclass(frozen=True)
class LeanObligation:
    """A proof obligation represented in Lean."""

    obligation_id: str
    theorem_name: str
    proposition: str
    context: Dict[str, str] = field(default_factory=dict)
    source_ref: str = ""


@dataclass
class LeanArtifact:
    """Structured Lean output with both text and metadata."""

    module_name: str
    imports: List[str] = field(default_factory=lambda: ["Mathlib"])
    claims: List[LeanClaim] = field(default_factory=list)
    obligations: List[LeanObligation] = field(default_factory=list)
    metadata: Dict[str, Any] = field(default_factory=dict)

    def to_text(self) -> str:
        lines: List[str] = [
            "-- Auto-generated by deppy.hybrid.lean_translation.LeanEmitter",
            f"-- generated_at: {datetime.now(timezone.utc).isoformat()}",
            "",
        ]
        lines.extend(f"import {imp}" for imp in self.imports)
        lines.append("")
        lines.append(f"namespace {self.module_name}")
        lines.append("")
        lines.extend(self._emit_claims())
        lines.extend(self._emit_obligations())
        lines.append(f"end {self.module_name}")
        return "\n".join(lines).strip() + "\n"

    def _emit_claims(self) -> List[str]:
        out: List[str] = []
        for claim in self.claims:
            out.append(f"/-- source: {claim.source or 'unknown'} | trust: {claim.trust_level} -/")
            theorem_head = f"theorem {claim.name}"
            if claim.assumptions:
                args = " ".join(f"({a})" for a in claim.assumptions)
                theorem_head += f" {args}"
            theorem_head += f" : {claim.proposition} := by"
            out.append(theorem_head)
            if claim.proof_hint:
                out.append(f"  -- hint: {claim.proof_hint}")
            out.append("  sorry")
            out.append("")
        return out

    def _emit_obligations(self) -> List[str]:
        out: List[str] = []
        if self.obligations:
            out.append("-- Obligations")
        for obl in self.obligations:
            out.append(f"/-- obligation: {obl.obligation_id} @ {obl.source_ref or 'unknown'} -/")
            out.append(f"theorem {obl.theorem_name} : {obl.proposition} := by")
            if obl.context:
                ctx = ", ".join(f"{k}: {v}" for k, v in sorted(obl.context.items()))
                out.append(f"  -- context: {ctx}")
            out.append("  sorry")
            out.append("")
        return out


class LeanEmitter:
    """Emit Lean artifacts from claims and deppy obligations."""

    def emit_claims(self, claims: Iterable[LeanClaim], *, module_name: str = "DeppyHybrid") -> LeanArtifact:
        artifact = LeanArtifact(module_name=module_name)
        artifact.claims.extend(list(claims))
        artifact.metadata["claim_count"] = len(artifact.claims)
        return artifact

    def emit_obligations(self, obligations: Iterable[LeanObligation], *, module_name: str = "DeppyHybrid") -> LeanArtifact:
        artifact = LeanArtifact(module_name=module_name)
        artifact.obligations.extend(list(obligations))
        artifact.metadata["obligation_count"] = len(artifact.obligations)
        return artifact

    def emit_from_hybrid_obligations(
        self,
        obligations: Iterable[Any],
        *,
        module_name: str = "DeppyHybrid",
    ) -> LeanArtifact:
        transformed: List[LeanObligation] = []
        for idx, obl in enumerate(obligations):
            uid = getattr(obl, "uid", f"obl_{idx}")
            theorem_name = f"{uid}_sound".replace("-", "_")
            proposition = self._obligation_to_prop(obl)
            context = dict(getattr(obl, "context_variables", {}) or {})
            src = str(getattr(obl, "location", ""))
            transformed.append(
                LeanObligation(
                    obligation_id=uid,
                    theorem_name=theorem_name,
                    proposition=proposition,
                    context=context,
                    source_ref=src,
                )
            )
        artifact = LeanArtifact(module_name=module_name, obligations=transformed)
        artifact.metadata["obligation_count"] = len(transformed)
        return artifact

    @staticmethod
    def _obligation_to_prop(obligation: Any) -> str:
        text = getattr(obligation, "assertion_text", "") or getattr(obligation, "description", "")
        if text:
            return f"Prop -- {text}"
        return "True"

    def render_text(
        self,
        *,
        claims: Optional[Iterable[LeanClaim]] = None,
        obligations: Optional[Iterable[LeanObligation]] = None,
        module_name: str = "DeppyHybrid",
    ) -> str:
        artifact = LeanArtifact(module_name=module_name)
        if claims:
            artifact.claims.extend(list(claims))
        if obligations:
            artifact.obligations.extend(list(obligations))
        artifact.metadata["claim_count"] = len(artifact.claims)
        artifact.metadata["obligation_count"] = len(artifact.obligations)
        return artifact.to_text()


__all__ = [
    "LeanClaim",
    "LeanObligation",
    "LeanArtifact",
    "LeanEmitter",
]

