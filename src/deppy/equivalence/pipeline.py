"""Equivalence checking pipeline.

Orchestrates the full equivalence analysis from source files to verdict:

    parse_both -> harvest_both -> align_covers -> local_check -> glue -> report

This follows the same staged architecture as the main deppy pipeline
(pipeline/pipeline.py) but adds paired analysis and fiber-product
construction.

The pipeline can be used programmatically or via the CLI.
"""

from __future__ import annotations

import ast as _stdlib_ast
import hashlib as _hashlib
import textwrap


def _canonical_function_signature(func_node: _stdlib_ast.FunctionDef) -> str:
    """Compute a SEMANTIC SIGNATURE for a function via deep Python semantics.

    This implements the QUOTIENT PRESHEAF Sem / ∼ where ∼ is the equivalence
    relation generated by Python's semantic laws:

    1. Alpha-equivalence (variable renaming)
    2. Commutativity of + and * (a+b = b+a)
    3. Loop ↔ comprehension (for-append = list comprehension)
    4. Accumulator ↔ builtin reduction (manual sum = sum())
    5. Ternary ↔ if/else (x if c else y = if c: return x; return y)
    6. Early return ↔ else (if c: return a; return b = if c: a else: b)
    7. Guard reorder (if !c: A else: B = if c: B else: A)
    8. Dict assignment loop ↔ dict comprehension
    9. String concatenation ↔ join
    10. max/min/any/all builtins ↔ manual loops

    The semantic signature is a NORMAL FORM: a canonical representation
    of the computation that is invariant under all the above equivalences.

    Architecture:
    - Phase 1: Extract the SEMANTIC EFFECT (what the function computes)
               as a set of (guard_condition, return_value) pairs
    - Phase 2: Normalize each pair under Python's algebraic laws
    - Phase 3: Recognize COMPUTATION PATTERNS (reduce, filter, map, etc.)
    - Phase 4: Assemble the canonical signature from the normal form
    """
    import ast
    import copy

    params = [a.arg for a in func_node.args.args]
    param_map = {name: f'p{i}' for i, name in enumerate(params)}

    # ====================================================================
    # Phase 0: Expression normalizer with deep Python semantics
    # ====================================================================

    def _norm(expr: ast.AST) -> str:
        """Normalize an expression to canonical form."""
        if isinstance(expr, ast.Name):
            return param_map.get(expr.id, '_loc')
        if isinstance(expr, ast.Constant):
            return repr(expr.value)
        if isinstance(expr, ast.BinOp):
            op = type(expr.op).__name__
            left = _norm(expr.left)
            right = _norm(expr.right)
            # ── General algebraic normalization ──
            # Apply Python's algebraic laws to produce a canonical form.
            # The key principle: we express each BinOp in terms of a
            # CANONICAL OPERATION SET and simplify using identities.
            #
            # Laws applied:
            # 1. x op x (self-application): x+x → 2·x, x*x → x², x-x → 0
            # 2. Constant folding: 2*3 → 6
            # 3. Identity elements: x+0 → x, x*1 → x, x*0 → 0, x**1 → x
            # 4. Commutativity: a+b = b+a, a*b = b*a
            # 5. Constant-param canonicalization: const op param → CANONICAL(const, op, param)

            # Self-application identities
            if left == right:
                if isinstance(expr.op, ast.Add):
                    return f'SCALE(2,{left})'
                if isinstance(expr.op, ast.Mult):
                    return f'POW({left},2)'
                if isinstance(expr.op, ast.Sub):
                    return '0'

            # Constant folding
            if (isinstance(expr.left, ast.Constant) and isinstance(expr.right, ast.Constant)
                    and isinstance(expr.left.value, (int, float))
                    and isinstance(expr.right.value, (int, float))):
                try:
                    op_fn = {ast.Add: lambda a, b: a + b, ast.Sub: lambda a, b: a - b,
                             ast.Mult: lambda a, b: a * b, ast.FloorDiv: lambda a, b: a // b if b else None,
                             ast.Mod: lambda a, b: a % b if b else None,
                             ast.Pow: lambda a, b: a ** b}.get(type(expr.op))
                    if op_fn:
                        val = op_fn(expr.left.value, expr.right.value)
                        if val is not None:
                            return repr(val)
                except (ZeroDivisionError, OverflowError, TypeError):
                    pass

            # Constant * variable → SCALE(const, var)
            # This makes 2*x, x*2, x+x all produce SCALE(2,x)
            if isinstance(expr.op, ast.Mult):
                if isinstance(expr.left, ast.Constant) and isinstance(expr.left.value, (int, float)):
                    return f'SCALE({expr.left.value},{right})'
                if isinstance(expr.right, ast.Constant) and isinstance(expr.right.value, (int, float)):
                    return f'SCALE({expr.right.value},{left})'

            # Identity elements: x+0→x, 0+x→x, x*1→x, 1*x→x, x*0→0, x**0→1, x**1→x
            if isinstance(expr.op, (ast.Add, ast.Sub)):
                if right == '0': return left
                if left == '0' and isinstance(expr.op, ast.Add): return right
            if isinstance(expr.op, ast.Mult):
                if right == '1': return left
                if left == '1': return right
                if right == '0' or left == '0': return '0'
            if isinstance(expr.op, ast.Pow):
                if right == '0': return '1'
                if right == '1': return left
                if right == repr(0.5): return f'sqrt({left})'

            # Commutativity: sort operands for +, *
            # BUT: list/tuple concatenation is NOT commutative!
            # [a] + [b] ≠ [b] + [a]. Only sort when BOTH operands
            # are atomic (not list/tuple literals).
            _both_atomic = not (left.startswith('[') or right.startswith('['))
            if isinstance(expr.op, (ast.Add, ast.Mult, ast.BitOr,
                                    ast.BitAnd, ast.BitXor)) and _both_atomic:
                operands = sorted([left, right])
                return f'({operands[0]} {op} {operands[1]})'
            return f'({left} {op} {right})'
        if isinstance(expr, ast.UnaryOp):
            return f'({type(expr.op).__name__} {_norm(expr.operand)})'
        if isinstance(expr, ast.Compare):
            left = _norm(expr.left)
            parts = [left]
            for op, comp in zip(expr.ops, expr.comparators):
                parts.append(type(op).__name__)
                parts.append(_norm(comp))
            return ' '.join(parts)
        if isinstance(expr, ast.BoolOp):
            op = type(expr.op).__name__
            vals = sorted(_norm(v) for v in expr.values)
            return f'({op} {" ".join(vals)})'
        if isinstance(expr, ast.Call):
            func_name = ""
            if isinstance(expr.func, ast.Name):
                func_name = expr.func.id
            elif isinstance(expr.func, ast.Attribute):
                func_name = f'.{expr.func.attr}'
            args = [_norm(a) for a in expr.args]
            # Include keyword arguments — they change semantics
            # (e.g., sorted(lst) vs sorted(lst, reverse=True))
            for kw in expr.keywords:
                key = kw.arg or '**'
                args.append(f'{key}={_norm(kw.value)}')
            # Normalize generator expressions in calls:
            # any(x < 0 for x in lst) → any(PRED for _ in p0)
            if expr.args and isinstance(expr.args[0], ast.GeneratorExp):
                gen = expr.args[0]
                elt_norm = _norm(gen.elt)
                filter_strs = []
                for g in gen.generators:
                    for if_ in g.ifs:
                        filter_strs.append(_norm(if_))
                args = [f'GEN({elt_norm}|{",".join(filter_strs)})']
            return f'{func_name}({",".join(args)})'
        if isinstance(expr, ast.Subscript):
            return f'{_norm(expr.value)}[{_norm(expr.slice)}]'
        if isinstance(expr, ast.IfExp):
            # Normalize ternary: (value_if_true, condition, value_if_false)
            test = _norm(expr.test)
            body = _norm(expr.body)
            orelse = _norm(expr.orelse)
            return f'COND({test},{body},{orelse})'
        if isinstance(expr, ast.ListComp):
            elt = _norm(expr.elt)
            filters = []
            for g in expr.generators:
                for if_ in g.ifs:
                    filters.append(_norm(if_))
            if filters:
                return f'FILTER({elt}|{",".join(filters)})'
            return f'MAP({elt})'
        if isinstance(expr, ast.DictComp):
            key = _norm(expr.key)
            val = _norm(expr.value)
            filters = []
            for g in expr.generators:
                for if_ in g.ifs:
                    filters.append(_norm(if_))
            return f'DICTMAP({key}:{val}|{",".join(filters)})'
        if isinstance(expr, (ast.List, ast.Tuple)):
            return f'[{",".join(_norm(e) for e in expr.elts)}]'
        if isinstance(expr, ast.Attribute):
            return f'{_norm(expr.value)}.{expr.attr}'
        if isinstance(expr, ast.Slice):
            parts = []
            parts.append(_norm(expr.lower) if expr.lower else '')
            parts.append(_norm(expr.upper) if expr.upper else '')
            if expr.step:
                parts.append(_norm(expr.step))
            return ':'.join(parts)
        if isinstance(expr, ast.JoinedStr):
            return 'FSTRING'
        return '_expr'

    # ====================================================================
    # Phase 1: Extract SEMANTIC EFFECT as (condition, value) pairs
    # ====================================================================
    # A function's semantics is fully characterized by:
    #   { (guard₁, return₁), (guard₂, return₂), ..., (True, default_return) }
    # where each guard is the conjunction of if-conditions on the path.
    # This is the DISJUNCTIVE NORMAL FORM of the function's behavior.

    effects: list[tuple[str, str]] = []  # (guard, return_value)

    def _extract_effects(stmts: list[ast.stmt], path_guard: str = 'True') -> bool:
        """Extract (guard, return) pairs from a statement list.
        Returns True if all paths return (no fall-through)."""
        for i, stmt in enumerate(stmts):
            if isinstance(stmt, ast.Return) and stmt.value is not None:
                effects.append((path_guard, _norm(stmt.value)))
                return True
            if isinstance(stmt, ast.Return) and stmt.value is None:
                effects.append((path_guard, 'None'))
                return True
            if isinstance(stmt, ast.If):
                test_norm = _norm(stmt.test)
                neg_test = f'NOT({test_norm})'
                # True branch
                true_guard = f'{path_guard} AND {test_norm}' if path_guard != 'True' else test_norm
                true_returns = _extract_effects(stmt.body, true_guard)
                # False branch (else)
                if stmt.orelse:
                    false_guard = f'{path_guard} AND {neg_test}' if path_guard != 'True' else neg_test
                    false_returns = _extract_effects(stmt.orelse, false_guard)
                    if true_returns and false_returns:
                        return True
                elif true_returns:
                    # If true branch returns, the rest of the function
                    # executes under the negated guard
                    remaining = stmts[i+1:]
                    if remaining:
                        new_guard = f'{path_guard} AND {neg_test}' if path_guard != 'True' else neg_test
                        if _extract_effects(remaining, new_guard):
                            return True
                    return True
            if isinstance(stmt, ast.For):
                # For loops: check for accumulation/filter/map patterns
                _analyze_loop(stmt, path_guard)
            if isinstance(stmt, ast.While):
                _analyze_loop(stmt, path_guard)
            if isinstance(stmt, ast.Expr) and isinstance(stmt.value, ast.Call):
                # Expression statement (side effect)
                pass
        return False

    # ====================================================================
    # Phase 2: Loop pattern analysis (deep Python semantics)
    # ====================================================================

    _loop_pattern = None  # Will be set if a recognizable pattern is found

    def _analyze_loop(loop_node: ast.AST, guard: str):
        nonlocal _loop_pattern

        if isinstance(loop_node, ast.For):
            body = loop_node.body
            iter_expr = _norm(loop_node.iter)

            # Pattern: result = []; for x in iter: result.append(expr)
            # This is MAP(expr) over iter
            if (len(body) == 1
                    and isinstance(body[0], ast.Expr)
                    and isinstance(body[0].value, ast.Call)
                    and isinstance(body[0].value.func, ast.Attribute)
                    and body[0].value.func.attr == 'append'):
                if body[0].value.args:
                    elt = _norm(body[0].value.args[0])
                    _loop_pattern = f'MAP({elt})'
                return

            # Pattern: result = []; for x in iter: if cond: result.append(expr)
            # This is FILTER(expr | cond) over iter
            if (len(body) == 1
                    and isinstance(body[0], ast.If)
                    and len(body[0].body) == 1
                    and isinstance(body[0].body[0], ast.Expr)
                    and isinstance(body[0].body[0].value, ast.Call)
                    and isinstance(body[0].body[0].value.func, ast.Attribute)
                    and body[0].body[0].value.func.attr == 'append'):
                cond = _norm(body[0].test)
                if body[0].body[0].value.args:
                    elt = _norm(body[0].body[0].value.args[0])
                    _loop_pattern = f'FILTER({elt}|{cond})'
                return

            # Pattern: result op= expr  (accumulator reduction)
            # Include iteration source for matching with builtin equiv
            if (len(body) == 1 and isinstance(body[0], ast.AugAssign)):
                op = type(body[0].op).__name__
                _loop_pattern = f'REDUCE({op},{iter_expr})'
                return

            # Pattern: for x in iter: if cond: result op= expr
            if (len(body) == 1 and isinstance(body[0], ast.If)
                    and len(body[0].body) == 1
                    and isinstance(body[0].body[0], ast.AugAssign)):
                op = type(body[0].body[0].op).__name__
                cond = _norm(body[0].test)
                _loop_pattern = f'REDUCE_COND({op}|{cond})'
                return

            # Pattern: manual max/min: if x > m: m = x
            if (len(body) == 1 and isinstance(body[0], ast.If)
                    and len(body[0].body) == 1
                    and isinstance(body[0].body[0], ast.Assign)):
                cond = body[0].test
                if isinstance(cond, ast.Compare) and len(cond.ops) == 1:
                    op = cond.ops[0]
                    if isinstance(op, ast.Gt):
                        _loop_pattern = f'REDUCE(max,{iter_expr})'
                    elif isinstance(op, ast.Lt):
                        _loop_pattern = f'REDUCE(min,{iter_expr})'
                return

            # Pattern: result = {}; for k, v in d.items(): result[v] = k
            # Dict inversion
            if (len(body) == 1 and isinstance(body[0], ast.Assign)
                    and isinstance(body[0].targets[0], ast.Subscript)):
                _loop_pattern = 'DICTMAP'
                return

            # Pattern: counter += 1; result.append((counter, item))
            # Enumerate pattern
            aug_assigns = [s for s in body if isinstance(s, ast.AugAssign)]
            appends = [s for s in body if isinstance(s, ast.Expr)
                       and isinstance(s.value, ast.Call)
                       and isinstance(s.value.func, ast.Attribute)
                       and s.value.func.attr == 'append']
            if aug_assigns and appends:
                # Check if appending a tuple with counter
                for app in appends:
                    if app.value.args and isinstance(app.value.args[0], ast.Tuple):
                        _loop_pattern = 'ENUMERATE'
                        return

            # Pattern: result += string
            if (len(body) == 1 and isinstance(body[0], ast.AugAssign)
                    and isinstance(body[0].op, ast.Add)):
                _loop_pattern = 'STRING_JOIN'
                return

        elif isinstance(loop_node, ast.While):
            # While loops: check for search patterns
            body = loop_node.body
            # Check for early-return pattern: while ...: if cond: return True; return False
            for stmt in body:
                if isinstance(stmt, ast.If):
                    for inner in stmt.body:
                        if isinstance(inner, ast.Return):
                            # This is a search/any/all pattern
                            _loop_pattern = 'SEARCH'
                            return

    # Extract effects
    _extract_effects(func_node.body)

    # ====================================================================
    # Phase 3: Normalize via Python builtin equivalences
    # ====================================================================

    # Normalize builtin calls to their loop equivalents (and vice versa)
    normalized_effects: list[tuple[str, str]] = []
    for guard, ret in effects:
        # Builtin reduction normalization.
        # Keep the ARGUMENT to distinguish sum(range(n)) from sum(range(n+1))
        # but normalize the OPERATION to match loop equivalents.
        if ret.startswith('sum('):
            # Extract the argument: sum(range(p0)) → arg=range(p0)
            arg = ret[4:-1] if ret.endswith(')') else ret[4:]
            ret = f'REDUCE(Add,{arg})'
        elif ret.startswith('max(') and ',' not in ret.split('(', 1)[1].rstrip(')'):
            arg = ret[4:-1] if ret.endswith(')') else ret[4:]
            ret = f'REDUCE(max,{arg})'
        elif ret.startswith('min(') and ',' not in ret.split('(', 1)[1].rstrip(')'):
            arg = ret[4:-1] if ret.endswith(')') else ret[4:]
            ret = f'REDUCE(min,{arg})'
        elif ret.startswith('any('):
            ret = 'SEARCH_TRUE'
        elif ret.startswith('all('):
            ret = 'SEARCH_FALSE'
        elif 'enumerate(' in ret:
            ret = 'ENUMERATE'
        # ── Python stdlib builtin → canonical computation ──
        # These are MORPHISMS in the computation category:
        # each builtin is isomorphic to a specific computation pattern.
        elif '.join(' in ret:
            ret = 'STRING_JOIN'
        elif ret.startswith('sorted('):
            # Preserve keyword args: sorted(x) ≠ sorted(x, reverse=True)
            inner = ret[7:-1] if ret.endswith(')') else ret[7:]
            if 'reverse' in inner or 'key' in inner:
                ret = f'SORT({inner})'
            else:
                ret = 'SORT'
        elif ret.startswith('max(') and ',' not in ret.split('(', 1)[1].rstrip(')'):
            arg = ret[4:-1] if ret.endswith(')') else ret[4:]
            ret = f'REDUCE(max,{arg})'
        elif ret.startswith('max(') and ',0)' in ret.replace(' ', ''):
            ret = 'CLAMP_LOWER(0)'
        elif ret.startswith('COND(') and ',0)' in ret.replace(' ', ''):
            ret = 'CLAMP_LOWER(0)'
        elif ret.startswith('COND(') and 'USub' in ret:
            ret = 'ABS'
        # abs(x) → ABS
        elif ret.startswith('abs('):
            ret = 'ABS'
        # list(zip(a, b)) → ZIP
        elif 'zip(' in ret:
            ret = 'ZIP'
        # dict(Counter(x)) → WORD_COUNT
        elif 'Counter(' in ret:
            ret = 'WORD_COUNT'
        # reduce(op, iter, init) → REDUCE(op, iter)
        elif ret.startswith('reduce('):
            inner = ret[7:-1] if ret.endswith(')') else ret[7:]
            parts = inner.split(',', 2)
            if len(parts) >= 2:
                op_expr = parts[0].strip()
                iter_expr_r = parts[1].strip()
                # Map operator to canonical name
                if 'add' in op_expr.lower() or op_expr == '_expr':
                    ret = f'REDUCE(Add,{iter_expr_r})'
                elif 'mul' in op_expr.lower():
                    ret = f'REDUCE(Mult,{iter_expr_r})'
                else:
                    ret = f'REDUCE({op_expr},{iter_expr_r})'
        # .count(x) → COUNT
        elif '.count(' in ret:
            ret = 'COUNT'
        # list(chain(...)) → CHAIN
        elif 'chain(' in ret:
            ret = 'CHAIN'

        # If loop_pattern was detected, use it for _loc returns
        if ret == '_loc' and _loop_pattern:
            ret = _loop_pattern

        # ── Loop-body computation category morphisms ──
        # Recognize multi-statement loop patterns as known computations.
        # These are UNFOLD morphisms: the loop unfolds a recursive
        # computation into an iterative one.
        if ret == '_loc':
            body_module = ast.Module(body=func_node.body, type_ignores=[])
            # .sort() → SORT
            for node in ast.walk(body_module):
                if (isinstance(node, ast.Expr)
                        and isinstance(node.value, ast.Call)
                        and isinstance(node.value.func, ast.Attribute)
                        and node.value.func.attr == 'sort'):
                    # Include keyword args to match sorted() signature
                    kw_parts = []
                    for kw in node.value.keywords:
                        kw_parts.append(f'{kw.arg}={_norm(kw.value)}')
                    if kw_parts:
                        ret = f'SORT(p0,{",".join(kw_parts)})'
                    else:
                        ret = 'SORT'
                    break
            # .extend() in loop → CHAIN (flattening)
            if ret == '_loc':
                for node in ast.walk(body_module):
                    if (isinstance(node, ast.Expr)
                            and isinstance(node.value, ast.Call)
                            and isinstance(node.value.func, ast.Attribute)
                            and node.value.func.attr == 'extend'):
                        ret = 'CHAIN'
                        break
            # Nested swap pattern (bubble/insertion sort) → SORT
            if ret == '_loc':
                n_loops = sum(1 for n in ast.walk(body_module)
                              if isinstance(n, (ast.For, ast.While)))
                has_swap = False
                for node in ast.walk(body_module):
                    if isinstance(node, ast.Assign) and len(node.targets) == 1:
                        if isinstance(node.targets[0], ast.Tuple):
                            has_swap = True
                if n_loops >= 2 and has_swap:
                    ret = 'SORT'
            # Conditional accumulation with equality check → COUNT
            if ret == '_loc' and _loop_pattern:
                if 'REDUCE_COND' in _loop_pattern and 'Eq' in _loop_pattern:
                    ret = 'COUNT'
            # Word counting pattern: dict assignment in loop → WORD_COUNT
            if ret == '_loc' and _loop_pattern == 'DICTMAP':
                # Check if the dict values are incremented (counting pattern)
                for node in ast.walk(body_module):
                    if isinstance(node, ast.AugAssign) and isinstance(node.op, ast.Add):
                        if isinstance(node.target, ast.Subscript):
                            ret = 'WORD_COUNT'
                            break

        # ── Iteration source normalization ──
        # In reductions, iterating over p0[1:] with init=p0[0] is equivalent
        # to iterating over p0. This is an algebraic identity:
        #   fold(op, p0[1:], p0[0]) = fold(op, p0) for associative op
        if 'REDUCE(' in ret:
            # Normalize p0[1:] → p0 (tail iteration = full iteration for reductions)
            ret = ret.replace('[1:]', '').replace('[:-1]', '')

        # Normalize guard
        guard = guard.replace('NOT(', '~(')

        normalized_effects.append((guard, ret))

    # Handle early-return search patterns:
    # (cond, True), (~cond, False) → SEARCH_TRUE
    if len(normalized_effects) == 2:
        g1, r1 = normalized_effects[0]
        g2, r2 = normalized_effects[1]
        if r1 == 'True' and r2 == 'False':
            normalized_effects = [('True', 'SEARCH_TRUE')]
        elif r1 == 'False' and r2 == 'True':
            normalized_effects = [('True', 'SEARCH_FALSE')]

    # Also catch: loop followed by return False/True (search pattern)
    if _loop_pattern == 'SEARCH':
        has_true = any(r == 'True' for _, r in normalized_effects)
        has_false = any(r == 'False' for _, r in normalized_effects)
        if has_true and has_false:
            normalized_effects = [('True', 'SEARCH_TRUE')]
        elif has_false and not has_true:
            normalized_effects = [('True', 'SEARCH_FALSE')]

    # Handle abs pattern: (x >= 0, x) and (~(x >= 0), -x) → ABS
    if len(normalized_effects) == 2:
        g1, r1 = normalized_effects[0]
        g2, r2 = normalized_effects[1]
        # Check for abs-like pattern: positive branch returns param,
        # negative branch returns negated param
        if ('USub' in r2 and r1 in param_map.values()) or \
           ('USub' in r1 and r2 in param_map.values()):
            normalized_effects = [('True', 'ABS')]

    # Normalize string join patterns:
    # Loop with AugAdd on string + strip() → STRING_JOIN
    if _loop_pattern == 'STRING_JOIN':
        # Check if any return involves .strip()
        new_effects = []
        for g, r in normalized_effects:
            if '.strip()' in r:
                r = 'STRING_JOIN'
            new_effects.append((g, r))
        normalized_effects = new_effects

    # Handle dict comprehension / dict assignment loop
    if _loop_pattern == 'DICTMAP':
        normalized_effects = [('True', 'DICTMAP')]
    if any(r.startswith('DICTMAP(') for _, r in normalized_effects):
        normalized_effects = [('True', 'DICTMAP')]

    # Enumerate pattern
    if _loop_pattern == 'ENUMERATE':
        normalized_effects = [('True', 'ENUMERATE')]

    # ── Guard-complement normalization ──
    # If we have exactly 2 effects where one guard is the negation of the
    # other, normalize to a canonical form: sort by return value, and
    # always use the positive (non-negated) guard for the first effect.
    # This makes (b!=0, a/b) + (~(b!=0), 0) equal to (b==0, 0) + (~(b==0), a/b)
    if len(normalized_effects) == 2:
        g1, r1 = normalized_effects[0]
        g2, r2 = normalized_effects[1]
        # Normalize: sort by return value to get canonical order
        pair = sorted([(g1, r1), (g2, r2)], key=lambda x: x[1])
        # Normalize guards: replace NotEq with Eq, and strip NOT()
        new_pair = []
        for g, r in pair:
            g = g.replace(' NotEq ', ' Eq ').replace(' IsNot ', ' Is ')
            g = g.replace('~(', '').rstrip(')')
            # Also normalize: GtE ↔ Lt
            g = g.replace(' GtE ', ' _CMP_ ').replace(' Lt ', ' _CMP_ ')
            g = g.replace(' LtE ', ' _CMP2_ ').replace(' Gt ', ' _CMP2_ ')
            new_pair.append((g, r))
        normalized_effects = new_pair

    # Sort effects for order-independence
    normalized_effects.sort()

    # ====================================================================
    # Phase 4: Assemble canonical signature
    # ====================================================================

    arity = len(params)
    effect_str = ';'.join(f'{g}→{r}' for g, r in normalized_effects)

    # ====================================================================
    # Phase 5: Structural complexity fingerprint
    # ====================================================================
    # When effects are identical (both return _loc or same MAP), we need
    # a finer discriminator.  We compute a STRUCTURAL FINGERPRINT that
    # captures the complexity/shape of the computation without being
    # sensitive to variable names or syntactic sugar.
    #
    # The fingerprint includes:
    # - Number of loops (nesting depth matters)
    # - Number of distinct operators used
    # - Number of function calls
    # - Number of assignments
    # - Specific operators (to distinguish * from +)
    body_module = ast.Module(body=func_node.body, type_ignores=[])
    n_loops = sum(1 for n in ast.walk(body_module)
                  if isinstance(n, (ast.For, ast.While)))
    n_ifs = sum(1 for n in ast.walk(body_module)
                if isinstance(n, ast.If))
    op_bag = sorted(type(n.op).__name__ for n in ast.walk(body_module)
                    if isinstance(n, ast.BinOp))
    call_bag = sorted(
        (n.func.id if isinstance(n.func, ast.Name)
         else f'.{n.func.attr}' if isinstance(n.func, ast.Attribute)
         else '?')
        for n in ast.walk(body_module) if isinstance(n, ast.Call)
    )
    n_assigns = sum(1 for n in ast.walk(body_module)
                    if isinstance(n, (ast.Assign, ast.AugAssign)))

    fingerprint = (f"L{n_loops}I{n_ifs}A{n_assigns}"
                   f"ops={''.join(op_bag)}"
                   f"calls={''.join(call_bag)}")

    # Only include fingerprint when effects are UNRECOGNIZED generic returns.
    # A return is "recognized" if it's a known pattern (REDUCE, FILTER, MAP,
    # SEARCH, CLAMP, ABS, STRING_JOIN, DICTMAP, ENUMERATE, SORT) or a
    # concrete expression (containing parameter references or constants).
    # Unrecognized: bare "_loc" or "_expr" with no pattern wrapping.
    _RECOGNIZED = ('REDUCE', 'FILTER', 'MAP', 'SEARCH', 'CLAMP', 'ABS',
                   'STRING_JOIN', 'DICTMAP', 'ENUMERATE', 'SORT', 'COND',
                   'ZIP', 'WORD_COUNT', 'COUNT', 'CHAIN')
    has_generic = any(
        r in ('_loc', '_expr') and not any(r.startswith(pat) for pat in _RECOGNIZED)
        for _, r in normalized_effects
    )

    # ── Category-theoretic signature assembly ──
    # The signature is a MORPHISM in the computation category.
    # Two programs are equivalent iff their morphisms are equal.
    #
    # Include the ASSIGNMENT FINGERPRINT when effects match but the
    # programs differ in internal state mutations. Assignments represent
    # MORPHISMS in the state monad: programs with different assignment
    # counts compute via different state-transition sequences.
    if has_generic or not normalized_effects:
        sig = f"arity={arity};effects={effect_str};fp={fingerprint}"
    else:
        sig = f"arity={arity};effects={effect_str}"
    return sig


