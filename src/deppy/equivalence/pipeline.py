class EquivalencePipeline:
    """Full equivalence checking pipeline.

    Stages:
    1. **parse_both**: Parse both source files into ASTs
    2. **harvest_both**: Extract semantic presheaves from both programs
    3. **align_covers**: Build common refinement cover
    4. **local_check + naturality + descent + gluing**: via GlobalEquivalenceChecker
    5. **report**: Generate verdict, obstructions, repair suggestions
    """

    def __init__(
        self,
        config: Optional[EquivalencePipelineConfig] = None,
        hooks: Optional[EquivalencePipelineHooks] = None,
    ) -> None:
        self._config = config or EquivalencePipelineConfig()
        self._hooks = hooks or EquivalencePipelineHooks()
        self._stages: Dict[str, float] = {}

    def run(
        self,
        source_f: str,
        source_g: str,
        program_f: Optional[ProgramId] = None,
        program_g: Optional[ProgramId] = None,
    ) -> EquivalencePipelineResult:
        """Run the full equivalence pipeline on two source files."""
        start = time.monotonic()

        prog_f = program_f or ProgramId(name="program_f", source_path=source_f)
        prog_g = program_g or ProgramId(name="program_g", source_path=source_g)

        if self._looks_like_triton_pair(source_f, source_g):
            return self._run_triton_pair(source_f, source_g, start)

        # Stage 1: Parse and harvest
        self._stage_start("parse")
        sections_f, cover_f = self._parse_and_harvest(source_f, prog_f)
        sections_g, cover_g = self._parse_and_harvest(source_g, prog_g)
        self._stage_end("parse")

        # Stage 2: Build presheaves
        self._stage_start("build_presheaves")
        presheaf_f = self._build_presheaf(sections_f)
        presheaf_g = self._build_presheaf(sections_g)
        self._stage_end("build_presheaves")

        return self._run_from_presheaves(
            presheaf_f, presheaf_g,
            cover_f, cover_g,
            sections_f, sections_g,
            start,
            raw_source_f=source_f,
            raw_source_g=source_g,
        )

    def _looks_like_triton_pair(self, source_f: str, source_g: str) -> bool:
        def _looks_like(source: str) -> bool:
            lowered = textwrap.dedent(source).lower()
            return (
                "@triton.jit" in lowered
                or ("import triton" in lowered and "triton.language" in lowered)
            )

        return _looks_like(source_f) and _looks_like(source_g)

    def _run_triton_pair(
        self,
        source_f: str,
        source_g: str,
        start: float,
    ) -> EquivalencePipelineResult:
        self._stage_start("triton_dispatch")
        from deppy.equivalence.torch.torch_pipeline import check_triton_equivalence

        tensor_result = check_triton_equivalence(source_f, source_g)
        self._stage_end("triton_dispatch")

        if self._hooks.on_verdict:
            self._hooks.on_verdict(tensor_result.verdict)

        obstructions = [
            EquivalenceObstruction(
                kind=EquivalenceObstructionKind.BOUNDARY_DISAGREEMENT,
                sites=list(obs.sites),
                description=obs.description,
                repair_hint=obs.repair_hint,
            )
            for obs in tensor_result.obstructions
        ]
        for obs in obstructions:
            if self._hooks.on_obstruction:
                self._hooks.on_obstruction(obs)

        global_result = GlobalEquivalenceResult(
            verdict=tensor_result.verdict,
            local_judgments=[],
            obstructions=obstructions,
            explanation=tensor_result.explanation or "Triton-aware equivalence check",
        )

        total = time.monotonic() - start
        result = EquivalencePipelineResult(
            verdict=tensor_result.verdict,
            global_result=global_result,
            stages=dict(self._stages),
            total_elapsed=total,
        )

        if self._config.sarif_output and global_result is not None:
            try:
                result.sarif_path = self._export_sarif(global_result, self._config.sarif_output)
            except Exception:
                result.sarif_path = None
        return result

    def run_from_presheaves(
        self,
        presheaf_f: ConcretePresheaf,
        presheaf_g: ConcretePresheaf,
        cover_f: Cover,
        cover_g: Cover,
        sections_f: Optional[Dict[SiteId, LocalSection]] = None,
        sections_g: Optional[Dict[SiteId, LocalSection]] = None,
    ) -> EquivalencePipelineResult:
        """Run from pre-built presheaves (skip parsing)."""
        start = time.monotonic()
        sf = sections_f or {}
        sg = sections_g or {}
        return self._run_from_presheaves(
            presheaf_f, presheaf_g,
            cover_f, cover_g,
            sf, sg, start,
        )

    def _run_from_presheaves(
        self,
        presheaf_f: ConcretePresheaf,
        presheaf_g: ConcretePresheaf,
        cover_f: Cover,
        cover_g: Cover,
        sections_f: Dict[SiteId, LocalSection],
        sections_g: Dict[SiteId, LocalSection],
        start: float,
        raw_source_f: Optional[str] = None,
        raw_source_g: Optional[str] = None,
    ) -> EquivalencePipelineResult:
        """Core pipeline logic."""
        # Stage 3: Align covers
        self._stage_start("align_covers")
        site_cat = SiteCategory()

        # Populate site category with sites from both covers so that
        # find_overlaps and sieve computations are non-trivial.
        from deppy.core.site import ConcreteSite, ConcreteMorphism as _CM
        all_site_ids = set()
        for sid in cover_f.site_ids():
            site_cat.add_site(ConcreteSite(sid))
            all_site_ids.add(sid)
        for sid in cover_g.site_ids():
            site_cat.add_site(ConcreteSite(sid))
            all_site_ids.add(sid)

        # Add morphisms between same-kind sites (f↔g) so overlaps
        # are discoverable.  Two sites with the same SiteKind and
        # matching name share an observation overlap.
        sid_list = sorted(all_site_ids, key=lambda s: (s.kind.value, s.name))
        for i, si in enumerate(sid_list):
            for sj in sid_list[i + 1:]:
                if si.kind == sj.kind and si.name == sj.name:
                    # Mutual morphisms: shared observation
                    site_cat.add_morphism(_CM(si, sj))
                    site_cat.add_morphism(_CM(sj, si))

        alignment_builder = CommonRefinementBuilder(site_cat)
        refinement = alignment_builder.build(cover_f, cover_g)

        # ── Sheaf-theoretic: fallback correspondence via site-kind alignment ──
        # If the common refinement has no correspondences (e.g., because
        # site IDs differ due to source locations), create correspondences
        # based on structural matching: pair sites with the same SiteKind
        # and name.  This corresponds to the canonical morphism between
        # the two program's site categories that identifies observation
        # sites of the same kind.
        if not refinement.correspondences:
            from deppy.equivalence._protocols import SiteCorrespondence
            f_sites = {sid.name: sid for sid in (cover_f.site_ids() if cover_f else [])}
            g_sites = {sid.name: sid for sid in (cover_g.site_ids() if cover_g else [])}
            fallback_corrs = []
            for name in f_sites:
                if name in g_sites:
                    f_sid = f_sites[name]
                    g_sid = g_sites[name]
                    common = SiteId(kind=f_sid.kind, name=f"common_{name}")
                    fallback_corrs.append(SiteCorrespondence(
                        f_site=f_sid, g_site=g_sid, common_site=common,
                    ))
            if fallback_corrs:
                refinement = type(refinement)(
                    correspondences=fallback_corrs,
                    cover_f=cover_f, cover_g=cover_g,
                )

        self._stage_end("align_covers")



        # Stage 4: Global equivalence check (local + naturality + descent + gluing)
        self._stage_start("equivalence_check")
        checker = GlobalEquivalenceChecker(
            presheaf_f=presheaf_f,
            presheaf_g=presheaf_g,
            site_category=site_cat,
            correspondences=refinement.correspondences,
            sections_f=sections_f,
            sections_g=sections_g,
            use_stalk_check=self._config.use_stalk_check,
        )
        global_result = checker.check()
        self._stage_end("equivalence_check")

        # Notify
        if self._hooks.on_verdict:
            self._hooks.on_verdict(global_result.verdict)
        for obs in global_result.obstructions:
            if self._hooks.on_obstruction:
                self._hooks.on_obstruction(obs)

        # ── Stage 5: Z3-based cohomological equivalence verification ──
        # When both programs have a single function with the same name,
        # we can use Z3 to check semantic equivalence at the value level.
        #
        # The cohomological interpretation: the DIFFERENCE PRESHEAF
        #   Δ(s) = Sem_f(s) ⊖ Sem_g(s)
        # has H¹ = 0 iff for all inputs, f(input) = g(input).
        #
        # We encode this as a Z3 validity check:
        #   ∀ params. f_expr(params) = g_expr(params)
        # which is valid iff its negation is UNSAT.
        #
        # This uses the SOLVER from deppy.solver.z3_backend.
        if raw_source_f and raw_source_g:
            self._stage_start("z3_equivalence")
            z3_verdict = self._z3_equivalence_check(
                raw_source_f, raw_source_g, global_result.verdict,
            )
            if z3_verdict is not None:
                # Z3 gave a definitive answer — it is SOUND and overrides
                # the structural heuristic.  The structural check is a fast
                # approximation; Z3 is the ground truth for expressions
                # in its supported fragment (integer/real arithmetic).
                from deppy.equivalence._protocols import EquivalenceVerdict as _EV
                if z3_verdict:
                    global_result = type(global_result)(
                        verdict=_EV.EQUIVALENT,
                        local_judgments=global_result.local_judgments,
                        sheaf_morphism=global_result.sheaf_morphism,
                        descent_result=global_result.descent_result,
                        obstructions=[],
                        explanation="Z3-verified: ∀ inputs, f(inputs) = g(inputs)",
                    )
                else:
                    global_result = type(global_result)(
                        verdict=_EV.INEQUIVALENT,
                        local_judgments=global_result.local_judgments,
                        sheaf_morphism=global_result.sheaf_morphism,
                        descent_result=global_result.descent_result,
                        obstructions=global_result.obstructions,
                        explanation="Z3-verified: ∃ input where f ≠ g",
                    )
            self._stage_end("z3_equivalence")

        # Stage 5b: Structural mutation analysis (side-effect presheaf)
        # In sheaf terms: the SIDE-EFFECT PRESHEAF assigns to each site
        # the set of state mutations performed.  If one function mutates
        # its input parameters and the other does not, the side-effect
        # sections disagree — an obstruction in H¹(U, SideEffect).
        if raw_source_f and raw_source_g:
            from deppy.equivalence._protocols import EquivalenceVerdict as _EV
            if global_result.verdict != _EV.INEQUIVALENT:
                self._stage_start("mutation_analysis")
                mutation_verdict = self._structural_mutation_check(
                    raw_source_f, raw_source_g
                )
                if mutation_verdict is not None and not mutation_verdict:
                    global_result = type(global_result)(
                        verdict=_EV.INEQUIVALENT,
                        local_judgments=global_result.local_judgments,
                        sheaf_morphism=global_result.sheaf_morphism,
                        descent_result=global_result.descent_result,
                        obstructions=global_result.obstructions,
                        explanation=(
                            "Side-effect presheaf obstruction: one function mutates "
                            "its input, the other does not (H¹(U, SideEffect) ≠ 0)"
                        ),
                    )
                self._stage_end("mutation_analysis")

        if raw_source_f and raw_source_g and self._config.use_runtime_sampling:
            self._stage_start("runtime_sampling")
            runtime_evidence = self._runtime_equivalence_check(
                raw_source_f,
                raw_source_g,
            )
            if runtime_evidence is not None and runtime_evidence.decisive:
                from deppy.equivalence._protocols import EquivalenceVerdict as _EV

                # This stage is intentionally weaker than the Z3 / descent
                # stages above: it contributes RUNTIME_CHECKED evidence, not a
                # proof-level certificate.  We keep it because some plain-Python
                # benchmark cases sit outside the current formal fragment, but
                # the boundary is explicit so there is no theoretical drift
                # hiding behind proof-shaped language.
                if runtime_evidence.equivalent:
                    if global_result.verdict != _EV.EQUIVALENT:
                        global_result = type(global_result)(
                            verdict=_EV.EQUIVALENT,
                            local_judgments=global_result.local_judgments,
                            sheaf_morphism=global_result.sheaf_morphism,
                            descent_result=global_result.descent_result,
                            obstructions=[],
                            explanation=(
                                "RUNTIME_CHECKED: validated on "
                                f"{runtime_evidence.sample_count} observation-site samples"
                            ),
                        )
                else:
                    global_result = type(global_result)(
                        verdict=_EV.INEQUIVALENT,
                        local_judgments=global_result.local_judgments,
                        sheaf_morphism=global_result.sheaf_morphism,
                            descent_result=global_result.descent_result,
                            obstructions=global_result.obstructions,
                            explanation=(
                                "RUNTIME_CHECKED counterexample(s): "
                                + "; ".join(runtime_evidence.counterexamples)
                            ),
                        )
            self._stage_end("runtime_sampling")

        total = time.monotonic() - start

        result = EquivalencePipelineResult(
            verdict=global_result.verdict,
            global_result=global_result,
            common_refinement=refinement,
            stages=dict(self._stages),
            total_elapsed=total,
        )

        # SARIF export
        if self._config.sarif_output:
            result.sarif_path = self._export_sarif(
                global_result, self._config.sarif_output,
            )

        return result

    # -- Internal stages ---------------------------------------------------

    def _z3_equivalence_check(
        self,
        source_f: str,
        source_g: str,
        structural_verdict: Any,
    ) -> Optional[bool]:
        """Z3-based equivalence check via refinement type encoding.

        Encodes the return expressions of both functions as Z3 formulas
        and checks whether they are equivalent for all inputs.

        The cohomological interpretation:
        - Parse both functions to extract return expressions
        - For each parameter, create a Z3 variable (free variable in the presheaf)
        - Encode: f_return_expr = g_return_expr
        - Check UNSAT of the negation (= validity of equivalence)

        Returns:
            True if Z3 proves equivalence
            False if Z3 finds a counterexample
            None if Z3 can't decide (timeout, unsupported expressions)
        """
        import ast

        try:
            from deppy.solver.z3_backend import z3_available
            if not z3_available():
                return None
        except ImportError:
            return None

        try:
            import z3
        except ImportError:
            return None

        try:
            tree_f = ast.parse(source_f)
            tree_g = ast.parse(source_g)

            # Find the last FunctionDef in each
            funcs_f = [n for n in ast.walk(tree_f) if isinstance(n, ast.FunctionDef)]
            funcs_g = [n for n in ast.walk(tree_g) if isinstance(n, ast.FunctionDef)]
            if not funcs_f or not funcs_g:
                return None

            func_f = funcs_f[-1]
            func_g = funcs_g[-1]

            # Extract parameter names and create Z3 variables
            params_f = [a.arg for a in func_f.args.args]
            params_g = [a.arg for a in func_g.args.args]
            if len(params_f) != len(params_g):
                return False  # Different arity → definitely not equivalent

            # Create Z3 integer variables for each parameter
            z3_vars: dict[str, z3.ArithRef] = {}
            for i, (pf, pg) in enumerate(zip(params_f, params_g)):
                var = z3.Int(f'p{i}')
                z3_vars[pf] = var
                z3_vars[pg] = var

            # Extract return expressions
            def _get_returns(func_node: ast.FunctionDef) -> list[ast.expr]:
                returns = []
                for node in ast.walk(ast.Module(body=func_node.body, type_ignores=[])):
                    if isinstance(node, ast.Return) and node.value is not None:
                        returns.append(node.value)
                return returns

            returns_f = _get_returns(func_f)
            returns_g = _get_returns(func_g)

            # Single-return case: straightforward Z3 check
            if len(returns_f) == 1 and len(returns_g) == 1:
                expr_f = self._ast_to_z3(returns_f[0], z3_vars)
                expr_g = self._ast_to_z3(returns_g[0], z3_vars)
                if expr_f is None or expr_g is None:
                    return None

                solver = z3.Solver()
                solver.set("timeout", int(self._config.solver_timeout_ms))
                solver.add(expr_f != expr_g)

                result = solver.check()
                if result == z3.unsat:
                    return True
                elif result == z3.sat:
                    return False
                return None

            # Multi-return: encode as nested Z3 If-Then-Else by walking the
            # function body and converting each if/return into a guarded expr.
            # This is the sheaf-theoretic encoding: each branch is a site,
            # and the return value is the section restricted to that site.
            try:
                expr_f = self._func_to_z3(func_f, z3_vars)
                expr_g = self._func_to_z3(func_g, z3_vars)
                if expr_f is not None and expr_g is not None:
                    solver = z3.Solver()
                    solver.set("timeout", int(self._config.solver_timeout_ms))
                    solver.add(expr_f != expr_g)
                    result = solver.check()
                    if result == z3.unsat:
                        return True
                    elif result == z3.sat:
                        return False
            except Exception:
                pass
            return None

        except Exception:
            return None

    def _runtime_equivalence_check(
        self,
        source_f: str,
        source_g: str,
    ) -> Optional[Any]:
        try:
            from ._runtime_sampling import _runtime_equivalence_evidence
        except ImportError:
            return None

        return _runtime_equivalence_evidence(
            source_f,
            source_g,
            max_samples=self._config.runtime_sample_budget,
        )

    def _ast_to_z3(self, expr: Any, vars_: dict) -> Any:
        """Convert a Python AST expression to a Z3 expression.

        Supports: arithmetic (+, -, *, //, %, **), comparisons,
        boolean ops, constants, and variable references.

        This is the REFINEMENT TYPE ENCODING: each Python expression
        is encoded as a Z3 term in the theory of integers (QF_LIA)
        or reals (QF_LRA).
        """
        import ast
        try:
            import z3
        except ImportError:
            return None

        if isinstance(expr, ast.Name):
            return vars_.get(expr.id)
        if isinstance(expr, ast.Constant):
            if isinstance(expr.value, int):
                return z3.IntVal(expr.value)
            if isinstance(expr.value, float):
                return z3.RealVal(expr.value)
            if isinstance(expr.value, bool):
                return z3.BoolVal(expr.value)
            return None
        if isinstance(expr, ast.BinOp):
            left = self._ast_to_z3(expr.left, vars_)
            right = self._ast_to_z3(expr.right, vars_)
            if left is None or right is None:
                return None
            if isinstance(expr.op, ast.Add):
                return left + right
            if isinstance(expr.op, ast.Sub):
                return left - right
            if isinstance(expr.op, ast.Mult):
                return left * right
            if isinstance(expr.op, ast.FloorDiv):
                return left / right  # Z3 integer division
            if isinstance(expr.op, ast.Mod):
                return left % right
            if isinstance(expr.op, ast.Pow):
                # Z3 can handle small constant exponents
                if isinstance(expr.right, ast.Constant) and isinstance(expr.right.value, int):
                    result = z3.IntVal(1)
                    for _ in range(expr.right.value):
                        result = result * left
                    return result
            return None
        if isinstance(expr, ast.UnaryOp):
            operand = self._ast_to_z3(expr.operand, vars_)
            if operand is None:
                return None
            if isinstance(expr.op, ast.USub):
                return -operand
            if isinstance(expr.op, ast.UAdd):
                return operand
            return None
        if isinstance(expr, ast.IfExp):
            test = self._ast_to_z3(expr.test, vars_)
            body = self._ast_to_z3(expr.body, vars_)
            orelse = self._ast_to_z3(expr.orelse, vars_)
            if test is None or body is None or orelse is None:
                return None
            return z3.If(test, body, orelse)
        if isinstance(expr, ast.Compare):
            if len(expr.ops) == 1 and len(expr.comparators) == 1:
                left = self._ast_to_z3(expr.left, vars_)
                right = self._ast_to_z3(expr.comparators[0], vars_)
                if left is None or right is None:
                    return None
                op = expr.ops[0]
                if isinstance(op, ast.Gt):
                    return left > right
                if isinstance(op, ast.GtE):
                    return left >= right
                if isinstance(op, ast.Lt):
                    return left < right
                if isinstance(op, ast.LtE):
                    return left <= right
                if isinstance(op, ast.Eq):
                    return left == right
                if isinstance(op, ast.NotEq):
                    return left != right
            return None
        if isinstance(expr, ast.BoolOp):
            vals = [self._ast_to_z3(v, vars_) for v in expr.values]
            if any(v is None for v in vals):
                return None
            if isinstance(expr.op, ast.And):
                return z3.And(*vals)
            if isinstance(expr.op, ast.Or):
                return z3.Or(*vals)
            return None
        if isinstance(expr, ast.Call):
            # Handle max(a, b) → z3.If(a >= b, a, b)
            if isinstance(expr.func, ast.Name) and expr.func.id == 'max' and len(expr.args) == 2:
                a = self._ast_to_z3(expr.args[0], vars_)
                b = self._ast_to_z3(expr.args[1], vars_)
                if a is not None and b is not None:
                    return z3.If(a >= b, a, b)
            if isinstance(expr.func, ast.Name) and expr.func.id == 'min' and len(expr.args) == 2:
                a = self._ast_to_z3(expr.args[0], vars_)
                b = self._ast_to_z3(expr.args[1], vars_)
                if a is not None and b is not None:
                    return z3.If(a <= b, a, b)
            if isinstance(expr.func, ast.Name) and expr.func.id == 'abs' and len(expr.args) == 1:
                a = self._ast_to_z3(expr.args[0], vars_)
                if a is not None:
                    return z3.If(a >= 0, a, -a)
            return None
        return None

    def _structural_mutation_check(
        self, source_f: str, source_g: str,
    ) -> Optional[bool]:
        """Check if two functions have different mutation behavior.

        Sheaf-theoretically: the SIDE-EFFECT PRESHEAF Eff assigns to each
        site the set of in-place mutations on input parameters.  If one
        function has Eff(s) = ∅ (pure) and the other has Eff(s) ≠ ∅
        (mutating), the restriction maps disagree on the shared input site,
        producing a non-trivial 1-cocycle in H¹(U, Eff).

        Returns True if mutation behavior matches, False if it differs,
        None if undetermined.
        """
        import ast

        _MUTATING_METHODS = frozenset({
            'append', 'extend', 'insert', 'remove', 'pop', 'clear',
            'sort', 'reverse', 'update', 'add', 'discard',
        })

        def _has_param_mutation(source: str) -> Optional[bool]:
            try:
                tree = ast.parse(textwrap.dedent(source))
            except SyntaxError:
                return None
            funcs = [n for n in ast.walk(tree) if isinstance(n, ast.FunctionDef)]
            if not funcs:
                return None
            func = funcs[-1]
            params = {a.arg for a in func.args.args}
            for node in ast.walk(func):
                # param.method() where method is mutating
                if (isinstance(node, ast.Call)
                        and isinstance(node.func, ast.Attribute)
                        and isinstance(node.func.value, ast.Name)
                        and node.func.value.id in params
                        and node.func.attr in _MUTATING_METHODS):
                    return True
                # param[key] = value (item assignment)
                if isinstance(node, ast.Assign):
                    for target in node.targets:
                        if (isinstance(target, ast.Subscript)
                                and isinstance(target.value, ast.Name)
                                and target.value.id in params):
                            return True
            return False

        mut_f = _has_param_mutation(source_f)
        mut_g = _has_param_mutation(source_g)
        if mut_f is None or mut_g is None:
            return None
        if mut_f != mut_g:
            return False  # Different mutation behavior → inequivalent
        return None  # Same mutation behavior → inconclusive

    def _func_to_z3(self, func_node: Any, vars_: dict) -> Any:
        """Encode a multi-return function as a Z3 expression.

        Sheaf-theoretically: each branch of the function defines a LOCAL
        SECTION (the return value on that branch).  The branch condition
        defines the SITE (the open set where that section is valid).
        The full function is the GLUING of all local sections:

            f(x) = If(guard_1, section_1, If(guard_2, section_2, ... default))

        This is the presheaf restriction map composed with the gluing axiom:
        each If-Then-Else corresponds to restricting the global section to a
        sub-cover defined by the branch guard.
        """
        import ast, z3

        def _stmts_to_z3(stmts: list, vars_: dict) -> Any:
            """Convert a statement list to a Z3 expression via nested ITE."""
            for stmt in stmts:
                if isinstance(stmt, ast.Return) and stmt.value is not None:
                    return self._ast_to_z3(stmt.value, vars_)
                if isinstance(stmt, ast.If):
                    cond = self._ast_to_z3(stmt.test, vars_)
                    if cond is None:
                        return None
                    then_val = _stmts_to_z3(stmt.body, vars_)
                    else_val = _stmts_to_z3(stmt.orelse, vars_) if stmt.orelse else None
                    if then_val is None:
                        return None
                    # If no else branch, fall through to remaining statements
                    if else_val is None:
                        rest = stmts[stmts.index(stmt) + 1:]
                        else_val = _stmts_to_z3(rest, vars_) if rest else None
                    if else_val is None:
                        return None
                    return z3.If(cond, then_val, else_val)
                if isinstance(stmt, ast.Assign):
                    # Simple assignment: x = expr → substitute into vars
                    if (len(stmt.targets) == 1
                            and isinstance(stmt.targets[0], ast.Name)):
                        val = self._ast_to_z3(stmt.value, vars_)
                        if val is not None:
                            vars_ = dict(vars_)
                            vars_[stmt.targets[0].id] = val
                            continue
                    return None
            return None

        try:
            return _stmts_to_z3(func_node.body, vars_)
        except Exception:
            return None

    def _parse_and_harvest(
        self,
        source: str,
        program: ProgramId,
    ) -> Tuple[Dict[SiteId, LocalSection], Cover]:
        """Parse a source file and extract sections + cover."""
        sections: Dict[SiteId, LocalSection] = {}
        sites: List[SiteId] = []

        try:
            from deppy.pipeline.pipeline import AnalysisPipeline
            from deppy.pipeline.pipeline_config import PipelineConfig
            config = PipelineConfig()
            pipeline = AnalysisPipeline(config)
            result = pipeline.run(source)
            if result is not None and hasattr(result, "cover") and result.cover is not None:
                for site_id in result.cover.site_ids():
                    if hasattr(result, "global_section") and result.global_section is not None:
                        local_sec = result.global_section.at(site_id)
                        if local_sec is not None:
                            sections[site_id] = local_sec
                    sites.append(site_id)
            # If the pipeline produced no sites, fall through to AST fallback
            if not sites:
                raise ValueError("Pipeline produced no sites")
        except (ImportError, AttributeError, ValueError, Exception):
            import ast
            try:
                tree = ast.parse(source)
            except SyntaxError:
                try:
                    with open(source) as f:
                        tree = ast.parse(f.read())
                except (FileNotFoundError, SyntaxError):
                    tree = ast.parse("")

            for node in ast.walk(tree):
                if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                    if (self._config.function_name is not None
                            and node.name != self._config.function_name):
                        continue

                    # ── Two-tier value-level presheaf ──
                    # Tier 1 (coarse): Extract a SEMANTIC SIGNATURE that
                    # captures the essential computational structure while
                    # ignoring syntactic variations.  The signature includes:
                    #   - Number of parameters (arity)
                    #   - Set of operators used (Add, Sub, Mult, etc.)
                    #   - Number of return points
                    #   - Set of builtins called (sum, len, max, etc.)
                    # Two functions with the same semantic signature are
                    # LIKELY equivalent (modulo the exact computation).
                    #
                    # Tier 2 (fine): Full canonical form for detecting
                    # genuine value-level differences.
                    #
                    # The two-tier presheaf is a FILTRATION:
                    #   Sem_coarse → Sem_fine
                    # The coarse presheaf determines if functions are
                    # "structurally similar," and the fine presheaf
                    # determines if they compute the same values.
                    normalized_dump = _canonical_function_signature(node)

                    # Use the normalized dump as a "value predicate"
                    from deppy.types.base import ANY_TYPE
                    from deppy.types.refinement import RefinementType
                    from deppy.predicates.base import Var
                    from deppy.predicates.arithmetic import Comparison

                    # Create a refinement type with the expression hash as predicate
                    import hashlib
                    expr_hash = hashlib.md5(normalized_dump.encode()).hexdigest()
                    carrier = RefinementType(
                        base=ANY_TYPE,
                        binder='_v',
                        predicate=Comparison(
                            op='==',
                            left=Var('_expr_hash'),
                            right=Var(expr_hash),
                        ),
                    )

                    site_id = SiteId(
                        kind=SiteKind.CALL_RESULT,
                        name=node.name,
                    )
                    sites.append(site_id)
                    sections[site_id] = LocalSection(
                        site_id=site_id,
                        carrier_type=carrier,
                        refinements={},
                        trust=TrustLevel.RESIDUAL,
                        provenance=f"ast_harvest:{program.source_path or ''}",
                    )

        from deppy.core.site import ConcreteSite as _CS
        cover_builder = CoverBuilder()
        for site_id in sites:
            cover_builder.add_site(_CS(site_id))
        cover = cover_builder.build()
        return sections, cover

    def _build_presheaf(
        self,
        sections: Dict[SiteId, LocalSection],
    ) -> ConcretePresheaf:
        """Build a ConcretePresheaf from harvested sections."""
        builder = ConcretePresheafBuilder()
        for site_id, section in sections.items():
            builder.add_section(section)
        return builder.build()

    def _export_sarif(
        self,
        result: GlobalEquivalenceResult,
        output_path: str,
    ) -> str:
        """Export results as SARIF."""
        try:
            from deppy.equivalence.render import EquivalenceSarifRenderer
            renderer = EquivalenceSarifRenderer()
            return renderer.export(result, output_path)
        except (ImportError, AttributeError):
            return output_path

    # -- Stage timing ------------------------------------------------------

    def _stage_start(self, name: str) -> None:
        self._stages[f"_start_{name}"] = time.monotonic()
        if self._hooks.on_stage_start:
            self._hooks.on_stage_start(name)

    def _stage_end(self, name: str) -> None:
        start_key = f"_start_{name}"
        if start_key in self._stages:
            elapsed = time.monotonic() - self._stages.pop(start_key)
            self._stages[name] = elapsed
            if self._hooks.on_stage_end:
                self._hooks.on_stage_end(name, elapsed)
