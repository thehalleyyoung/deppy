class TensorSiteKind(enum.Enum):
    """Observation-site families for tensor programs.

    Each member corresponds to a **stratum** of the tensor-program site
    category.  Morphisms relate strata (e.g. broadcast : SHAPE × SHAPE → SHAPE).
    """

    SHAPE = "shape"
    """Dimensionality and extent constraints.  Section: {s : Shape | φ(s)}."""

    DTYPE = "dtype"
    """Element-type promotion lattice.  Section: {d : DType | φ(d)}."""

    DEVICE = "device"
    """Placement topology.  Section: {dev : Device | φ(dev)}."""

    STRIDE = "stride"
    """Memory layout invariants.  Section: {st : Strides | contiguous(st)}."""

    AUTOGRAD = "autograd"
    """Gradient computation graph.  Section: grad_fn DAG."""

    NUMERICAL = "numerical"
    """Floating-point value equivalence (within tolerance band)."""

    KERNEL_CONFIG = "kernel_config"
    """GPU kernel launch parameters (grid, block, shared mem)."""

    TRITON_BLOCK = "triton_block"
    """Triton block-parameter space (BLOCK_M, BLOCK_N, …)."""

    TRITON_MEMORY = "triton_memory"
    """Triton load / store / mask patterns."""

    TRITON_REDUCTION = "triton_reduction"
    """Parallel-reduction associativity semantics."""

    MLIR_OP = "mlir_op"
    """MLIR dialect operation semantics."""


class TensorStratum(enum.Enum):
    """Stratification of the tensor site category.

    The strata form a **partial order** reflecting information dependency:

        METADATA  ≤  STRUCTURAL  ≤  NUMERICAL  ≤  BEHAVIORAL

    Lower strata are necessary conditions for higher ones: if shapes
    disagree (STRUCTURAL), there is no point checking values (NUMERICAL).
    This ordering defines the **canonical filtration** of the site category
    and determines the order of local checks.
    """

    METADATA = 0
    """Device, dtype – cheapest to compare."""

    STRUCTURAL = 1
    """Shape, strides – structural compatibility."""

    NUMERICAL = 2
    """Floating-point values – requires tolerance model."""

    BEHAVIORAL = 3
    """Autograd, side-effects – most expensive."""


# Map each site kind to its stratum for filtration-ordered checking
SITE_KIND_STRATUM: Dict[TensorSiteKind, TensorStratum] = {
    TensorSiteKind.DEVICE: TensorStratum.METADATA,
    TensorSiteKind.DTYPE: TensorStratum.METADATA,
    TensorSiteKind.SHAPE: TensorStratum.STRUCTURAL,
    TensorSiteKind.STRIDE: TensorStratum.STRUCTURAL,
    TensorSiteKind.NUMERICAL: TensorStratum.NUMERICAL,
    TensorSiteKind.AUTOGRAD: TensorStratum.BEHAVIORAL,
    TensorSiteKind.KERNEL_CONFIG: TensorStratum.STRUCTURAL,
    TensorSiteKind.TRITON_BLOCK: TensorStratum.STRUCTURAL,
    TensorSiteKind.TRITON_MEMORY: TensorStratum.STRUCTURAL,
    TensorSiteKind.TRITON_REDUCTION: TensorStratum.NUMERICAL,
    TensorSiteKind.MLIR_OP: TensorStratum.STRUCTURAL,
}


