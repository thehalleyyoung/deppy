@dataclass
class TensorObstruction:
    """A tensor-specific equivalence obstruction.

    Like ``EquivalenceObstruction`` but enriched with tensor information:
    the witness input that triggers the divergence, the specific stratum
    where it was detected, and the numerical discrepancy.
    """

    kind: TensorObstructionKind
    stratum: TensorStratum
    sites: List[SiteId] = field(default_factory=list)
    description: str = ""
    witness_input: Optional[Any] = None  # The tensor input that triggered it
    output_f: Optional[Any] = None
    output_g: Optional[Any] = None
    max_abs_diff: Optional[float] = None
    max_rel_diff: Optional[float] = None
    max_ulp_diff: Optional[int] = None
    repair_hint: Optional[str] = None

    def to_equivalence_obstruction(self) -> EquivalenceObstruction:
        """Lift to the base obstruction type."""
        # Map tensor obstruction kinds to base equivalence kinds
        kind_map = {
            TensorObstructionKind.SHAPE_MISMATCH: EquivalenceObstructionKind.CARRIER_TYPE_MISMATCH,
            TensorObstructionKind.DTYPE_MISMATCH: EquivalenceObstructionKind.CARRIER_TYPE_MISMATCH,
            TensorObstructionKind.NUMERICAL_DIVERGENCE: EquivalenceObstructionKind.PREDICATE_GAP,
            TensorObstructionKind.GRADIENT_DIVERGENCE: EquivalenceObstructionKind.PREDICATE_GAP,
            TensorObstructionKind.AUTOGRAD_GRAPH_MISMATCH: EquivalenceObstructionKind.NATURALITY_FAILURE,
            TensorObstructionKind.TRITON_REDUCTION_ORDER: EquivalenceObstructionKind.PREDICATE_GAP,
            TensorObstructionKind.MLIR_LOWERING_MISMATCH: EquivalenceObstructionKind.NATURALITY_FAILURE,
        }
        base_kind = kind_map.get(
            self.kind, EquivalenceObstructionKind.PREDICATE_GAP
        )
        return EquivalenceObstruction(
            kind=base_kind,
            sites=self.sites,
            description=f"[{self.kind.value}] {self.description}",
            severity="high" if self.stratum.value >= TensorStratum.NUMERICAL.value else "medium",
            repair_hint=self.repair_hint,
        )


@dataclass
class TensorLocalJudgment:
    """Result of checking tensor equivalence at a single site/stratum.

    Enriches ``LocalEquivalenceJudgment`` with tensor-specific data:
    the stratum checked, witness tensors, and numerical discrepancy.
    """

    site_id: SiteId
    stratum: TensorStratum
    tensor_site_kind: TensorSiteKind
    verdict: EquivalenceVerdict
    explanation: str = ""
    obstructions: List[TensorObstruction] = field(default_factory=list)
    witness_input: Optional[Any] = None
    output_f: Optional[Any] = None
    output_g: Optional[Any] = None
    max_abs_diff: Optional[float] = None

    @property
    def is_equivalent(self) -> bool:
        return self.verdict == EquivalenceVerdict.EQUIVALENT

    def to_local_judgment(self) -> LocalEquivalenceJudgment:
        """Lift to the base LocalEquivalenceJudgment."""
        obligation = EquivalenceObligation(
            site_id=self.site_id,
            description=f"Tensor {self.tensor_site_kind.value} check",
            strength=EquivalenceStrength.DENOTATIONAL,
        )
        return LocalEquivalenceJudgment(
            site_id=self.site_id,
            verdict=self.verdict,
            obligation=obligation,
            explanation=self.explanation,
        )


@dataclass
class TensorDescentDatum:
    """Descent datum for tensor-program equivalence.

    On triple overlaps  U_i ∩ U_j ∩ U_k  (e.g. three Triton blocks
    that partially overlap in the computation domain), the transition
    isomorphisms must satisfy the cocycle condition:

        φ_{jk} ∘ φ_{ij} = φ_{ik}

    For tensor programs, the transition isomorphisms are typically
    tolerance-bounded identity maps, so the cocycle condition reduces
    to checking that tolerance accumulation doesn't exceed the budget.
    """

    local_judgments: Dict[SiteId, TensorLocalJudgment] = field(
        default_factory=dict
    )
    transition_isos: Dict[Tuple[SiteId, SiteId], Any] = field(
        default_factory=dict
    )
    cocycle_checks: Dict[Tuple[SiteId, SiteId, SiteId], bool] = field(
        default_factory=dict
    )
    tolerance_budget: Optional[float] = None

    def cocycle_holds(self) -> bool:
        if not self.cocycle_checks:
            return True
        return all(self.cocycle_checks.values())


@dataclass
class TensorCohomologyClass:
    """Element of H^n(U, Iso(Sem_f, Sem_g)) for tensor programs.

    H^0 = global tensor isomorphisms (the equivalence we seek)
    H^1 = obstructions to gluing local tensor equivalences

    For tensor programs, H^1 non-triviality typically arises from:
    - Numerical tolerance not being transitive (ε-chains)
    - Different reduction orders in parallel reductions
    - Device-transfer boundaries breaking contiguity
    """

    degree: int
    representative_sites: List[SiteId] = field(default_factory=list)
    is_trivial: bool = False
    obstruction_description: str = ""
    numerical_obstruction: Optional[float] = None

    @property
    def witnesses_equivalence(self) -> bool:
        return self.is_trivial

    @property
    def witnesses_obstruction(self) -> bool:
        return not self.is_trivial and self.degree >= 1


@dataclass
class TensorGlobalJudgment:
    """Global equivalence judgment for tensor programs.

    Aggregates local judgments across all strata and sites, sheaf morphism
    data, descent verification, and cohomological obstruction analysis.
    """

    verdict: EquivalenceVerdict
    program_f: ProgramId
    program_g: ProgramId
    strength: EquivalenceStrength = EquivalenceStrength.DENOTATIONAL

    # Per-stratum results
    stratum_results: Dict[TensorStratum, EquivalenceVerdict] = field(
        default_factory=dict
    )

    # Per-site results
    local_judgments: Dict[SiteId, TensorLocalJudgment] = field(
        default_factory=dict
    )

    # Sheaf morphism
    natural_transformation: Optional[NaturalTransformation] = None
    isomorphism_witness: Optional[IsomorphismWitness] = None

    # Descent and cohomology
    descent: Optional[TensorDescentDatum] = None
    cohomology: Optional[TensorCohomologyClass] = None

    # Obstructions
    obstructions: List[TensorObstruction] = field(default_factory=list)

    # Test inputs used
    test_inputs_used: int = 0

    explanation: str = ""

    @property
    def is_equivalent(self) -> bool:
        return self.verdict == EquivalenceVerdict.EQUIVALENT

    @property
    def sites_equivalent(self) -> FrozenSet[SiteId]:
        return frozenset(
            sid for sid, j in self.local_judgments.items() if j.is_equivalent
        )

    @property
    def sites_inequivalent(self) -> FrozenSet[SiteId]:
        return frozenset(
            sid
            for sid, j in self.local_judgments.items()
            if j.verdict == EquivalenceVerdict.INEQUIVALENT
        )

    def summary(self) -> str:
        total = len(self.local_judgments)
        eq = len(self.sites_equivalent)
        ineq = len(self.sites_inequivalent)
        parts = [
            f"{self.verdict.value}: {eq}/{total} sites equivalent",
            f"{ineq} inequivalent",
            f"{len(self.obstructions)} obstructions",
        ]
        if self.cohomology and self.cohomology.witnesses_obstruction:
            parts.append(f"H¹ non-trivial")
        return ", ".join(parts)

    def to_equivalence_judgment(self) -> EquivalenceJudgment:
        """Lift to the base EquivalenceJudgment."""
        base_locals = {
            sid: j.to_local_judgment()
            for sid, j in self.local_judgments.items()
        }
        return EquivalenceJudgment(
            verdict=self.verdict,
            program_f=self.program_f,
            program_g=self.program_g,
            strength=self.strength,
            local_judgments=base_locals,
            natural_transformation=self.natural_transformation,
            isomorphism_witness=self.isomorphism_witness,
            obstructions=[
                obs.to_equivalence_obstruction().to_obstruction_data()
                for obs in self.obstructions
            ],
        )


