class MLIRDialect(enum.Enum):
    """MLIR dialects relevant to Triton compilation.

    These form a **chain of categories** connected by lowering functors:

        tt  ──L₁──▶  ttg  ──L₂──▶  ttng  ──L₃──▶  llvm

    Each functor preserves semantics (natural iso) while changing the
    level of abstraction.
    """

    TRITON = "tt"            # Triton high-level dialect
    TRITON_GPU = "ttg"       # Triton GPU dialect (after tiling)
    TRITON_NVIDIA = "ttng"   # Triton NVIDIA-specific dialect
    LINALG = "linalg"        # MLIR linalg dialect
    SCFG = "scf"             # Structured control flow
    ARITH = "arith"          # Arithmetic operations
    MEMREF = "memref"        # Memory references
    LLVM = "llvm"            # LLVM IR dialect
    BUILTIN = "builtin"      # Built-in types / module


@dataclass(frozen=True)
class MLIROpSpec:
    """Specification of a single MLIR operation.

    Each operation is an **object** in the dialect category.  Operands and
    results define the morphisms (data-flow edges).
    """

    dialect: MLIRDialect
    op_name: str  # e.g. "tt.load", "ttg.local_alloc"
    operand_types: Tuple[str, ...] = ()  # MLIR type strings
    result_types: Tuple[str, ...] = ()
    attributes: Dict[str, Any] = field(default_factory=dict)
    regions: int = 0  # number of nested regions

    @property
    def full_name(self) -> str:
        return f"{self.dialect.value}.{self.op_name}"


@dataclass(frozen=True)
class MLIRLoweringStep:
    """A single step in a dialect-lowering chain.

    The lowering functor  L : Dialect_high → Dialect_low  maps each
    operation in the source dialect to one or more operations in the
    target dialect.  Semantic preservation means the presheaf of the
    lowered program is naturally isomorphic to the original.
    """

    source_dialect: MLIRDialect
    target_dialect: MLIRDialect
    pass_name: str  # e.g. "convert-triton-to-triton-gpu"
    preserves_semantics: bool = True
    metadata: Dict[str, Any] = field(default_factory=dict)


@dataclass
class MLIRModuleSpec:
    """Full specification of an MLIR module.

    An MLIR module is a **presheaf** over the dialect category: it assigns
    to each dialect the set of operations expressed in that dialect.
    The lowering chain transforms the module from high-level to low-level
    while preserving the presheaf structure (up to natural isomorphism).
    """

    name: str = ""
    dialect_ops: Dict[MLIRDialect, List[MLIROpSpec]] = field(
        default_factory=dict
    )
    lowering_chain: List[MLIRLoweringStep] = field(default_factory=list)
    source_text: Optional[str] = None

    @property
    def dialects_used(self) -> FrozenSet[MLIRDialect]:
        return frozenset(self.dialect_ops.keys())

    @property
    def op_count(self) -> int:
        return sum(len(ops) for ops in self.dialect_ops.values())

    def ops_in_dialect(self, dialect: MLIRDialect) -> List[MLIROpSpec]:
        return self.dialect_ops.get(dialect, [])


# ═══════════════════════════════════════════════════════════════════════════════
# General analysis modes
# ═══════════════════════════════════════════════════════════════════════════════


class AnalysisMode(enum.Enum):
    """What kind of program analysis to perform.

    Generalises beyond two-program equivalence:
    - EQUIVALENCE: natural isomorphism between two presheaves (classic).
    - BUG_FINDING: single presheaf fails sheaf condition (sections don't glue).
    - CORRECTNESS: morphism from spec-presheaf → impl-presheaf.
    - REFINEMENT: one-way behavioural inclusion (impl ≤ spec).
    - OPTIMIZATION: equivalence modulo performance-irrelevant strata.
    """

    EQUIVALENCE = "equivalence"
    BUG_FINDING = "bug_finding"
