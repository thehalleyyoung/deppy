# Numerical tolerance – the metric structure on the value presheaf
# ═══════════════════════════════════════════════════════════════════════════════


@dataclass(frozen=True)
class NumericalToleranceSpec:
    """Tolerance model for floating-point equivalence.

    The value presheaf  Val_f : Site → ℝ^n  is compared using a
    **tolerance relation**  ≈_ε  where:

        f(x) ≈_ε g(x)  ⟺  |f(x)_i - g(x)_i| ≤ atol + rtol·|g(x)_i|
                         ∧  ULP(f(x)_i, g(x)_i) ≤ max_ulp

    This relation is *not* transitive in general, but it defines a
    **Čech nerve** where simplices connect ε-close outputs.
    """

    rtol: float = 1e-5
    atol: float = 1e-8
    max_ulp: Optional[int] = None
    nan_equal: bool = True
    inf_equal: bool = True
    reduction_order_sensitive: bool = False
    dtype_specific_tol: Dict[str, Tuple[float, float]] = field(
        default_factory=dict
    )  # dtype → (rtol, atol)

    def tol_for_dtype(self, dtype: str) -> Tuple[float, float]:
        """Return (rtol, atol) appropriate for the given dtype."""
        if dtype in self.dtype_specific_tol:
            return self.dtype_specific_tol[dtype]
        if "float16" in dtype or "half" in dtype or "bfloat16" in dtype:
            return (1e-2, 1e-3)
        if "float64" in dtype or "double" in dtype:
            return (1e-9, 1e-12)
        return (self.rtol, self.atol)


