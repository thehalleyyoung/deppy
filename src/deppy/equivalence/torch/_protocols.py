@dataclass(frozen=True)
class TensorSpec:
    """Specification of a single tensor: the carrier type for tensor sites.

    A ``TensorSpec`` is a **refinement type** in disguise:
        {t : Tensor | shape(t) = shape ∧ dtype(t) = dtype ∧ device(t) = device
                      ∧ contiguous(t) = is_contiguous ∧ requires_grad(t) = rg}

    Each field corresponds to a section of the presheaf at the respective site.
    """

    shape: Optional[Tuple[int, ...]] = None
    dtype: Optional[str] = None  # "float32", "int64", …
    device: Optional[str] = None  # "cpu", "cuda:0", "mps", …
    requires_grad: Optional[bool] = None
    is_contiguous: Optional[bool] = None
    strides: Optional[Tuple[int, ...]] = None
    layout: Optional[str] = None  # "strided", "sparse_coo", …
    name: Optional[str] = None  # symbolic name for tracing

    @property
    def ndim(self) -> Optional[int]:
        return len(self.shape) if self.shape is not None else None

    @property
    def numel(self) -> Optional[int]:
        if self.shape is None:
            return None
        return math.prod(self.shape)

    def is_compatible_with(self, other: TensorSpec) -> bool:
        """Check if two specs could describe the same tensor."""
        for attr in ("shape", "dtype", "device", "requires_grad", "layout"):
            a, b = getattr(self, attr), getattr(other, attr)
            if a is not None and b is not None and a != b:
                return False
        return True

    def merge(self, other: TensorSpec) -> TensorSpec:
        """Merge two specs, preferring non-None values from self."""
        return TensorSpec(
            shape=self.shape if self.shape is not None else other.shape,
            dtype=self.dtype if self.dtype is not None else other.dtype,
            device=self.device if self.device is not None else other.device,
            requires_grad=(
                self.requires_grad
                if self.requires_grad is not None
                else other.requires_grad
            ),
            is_contiguous=(
                self.is_contiguous
                if self.is_contiguous is not None
                else other.is_contiguous
            ),
            strides=self.strides if self.strides is not None else other.strides,
            layout=self.layout if self.layout is not None else other.layout,
            name=self.name or other.name,
        )


@dataclass(frozen=True)
class TensorPair:
    """A pair of tensor specs (one from each program) at a common site.

    This is the tensor-level analog of ``SectionPair``: it records
    what each program promises about a specific tensor at a specific site.
    """

    site_id: SiteId
    spec_f: TensorSpec
    spec_g: TensorSpec

    @property
    def shapes_match(self) -> bool:
        if self.spec_f.shape is None or self.spec_g.shape is None:
            return True
        return self.spec_f.shape == self.spec_g.shape

    @property
    def dtypes_match(self) -> bool:
        if self.spec_f.dtype is None or self.spec_g.dtype is None:
            return True
        return self.spec_f.dtype == self.spec_g.dtype

    @property
    def devices_match(self) -> bool:
        if self.spec_f.device is None or self.spec_g.device is None:
            return True
        return self.spec_f.device == self.spec_g.device


@dataclass(frozen=True)
class TensorSignature:
    """Type signature of a tensor function: its input and output tensor specs.

    Models the presheaf's **boundary sections** — the input/output interface.
    """

    inputs: Tuple[TensorSpec, ...] = ()
    outputs: Tuple[TensorSpec, ...] = ()
    name: Optional[str] = None

    @property
    def arity(self) -> int:
        return len(self.inputs)


