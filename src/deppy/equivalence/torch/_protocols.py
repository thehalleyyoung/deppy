class TensorMorphismKind(enum.Enum):
    """Kinds of morphism in the tensor site category.

    Each morphism kind encodes a specific **restriction map** between
    tensor observation sites.  Together they generate all composable
    paths in the category.
    """

    BROADCAST = "broadcast"
    """Shape restriction via broadcasting rules.  (n,1)→(n,m)."""

    PROMOTE = "promote"
    """Dtype restriction via promotion lattice.  float32→float64."""

    TRANSFER = "transfer"
    """Device restriction via placement transfer.  cpu→cuda:0."""

    CHAIN_RULE = "chain_rule"
    """Autograd restriction via chain rule.  ∂(f∘g) = ∂f · ∂g."""

    CONTIGUITY = "contiguity"
    """Stride restriction to contiguity predicate."""

    TILE_REFINE = "tile_refine"
    """Kernel config → Triton block refinement."""

    MASK_SIEVE = "mask_sieve"
    """Triton block → masked sub-block (sieve in the topology)."""

    DIALECT_LOWER = "dialect_lower"
    """MLIR dialect lowering functor.  tt → ttg → llvm."""

    IDENTITY = "identity"
    """Identity morphism at any site."""

    COMPOSE = "compose"
    """Formal composition of two morphisms."""


@dataclass(frozen=True)
class TensorSiteMorphism:
    """A morphism in the tensor site category.

    Encodes how type information **restricts** from source to target site.
    The ``restriction_map`` is a callable that transforms a local section
    at the source into one at the target (preserving the presheaf structure).
    """

    source: SiteId
    target: SiteId
    kind: TensorMorphismKind
    restriction_map: Optional[Callable[[LocalSection], LocalSection]] = None
    metadata: Optional[Dict[str, Any]] = None

    def restrict(self, section: LocalSection) -> LocalSection:
        """Apply this morphism's restriction map to a section."""
        if self.restriction_map is not None:
            return self.restriction_map(section)
        return section

    def compose(self, other: TensorSiteMorphism) -> TensorSiteMorphism:
        """Compose self ∘ other (self after other)."""
        if self.restriction_map and other.restriction_map:
            f, g = self.restriction_map, other.restriction_map

            def composed(sec: LocalSection) -> LocalSection:
                return f(g(sec))
        else:
            composed = self.restriction_map or other.restriction_map

        return TensorSiteMorphism(
            source=other.source,
            target=self.target,
            kind=TensorMorphismKind.COMPOSE,
            restriction_map=composed,
            metadata={"factors": [other.kind.value, self.kind.value]},
        )


@dataclass
class TensorSiteCategory:
    """The site category for tensor programs.

    Objects are tensor observation sites.  Morphisms encode restriction
    maps.  The category carries a Grothendieck topology whose covers
    correspond to valid input decompositions (e.g. Triton block tilings).
    """

    sites: Dict[SiteId, TensorSiteKind] = field(default_factory=dict)
    morphisms: Dict[Tuple[SiteId, SiteId], TensorSiteMorphism] = field(
        default_factory=dict
    )
    _outgoing: Dict[SiteId, List[TensorSiteMorphism]] = field(
        default_factory=dict
    )

    def add_site(self, site_id: SiteId, kind: TensorSiteKind) -> None:
        self.sites[site_id] = kind

    def add_morphism(self, morphism: TensorSiteMorphism) -> None:
        key = (morphism.source, morphism.target)
        self.morphisms[key] = morphism
        self._outgoing.setdefault(morphism.source, []).append(morphism)

    def outgoing(self, site_id: SiteId) -> List[TensorSiteMorphism]:
        return self._outgoing.get(site_id, [])

    def find_path(
        self, start: SiteId, end: SiteId
    ) -> Optional[List[TensorSiteMorphism]]:
        """BFS for a morphism path from start to end."""
        if start == end:
            return []
        from collections import deque

        queue: deque[Tuple[SiteId, List[TensorSiteMorphism]]] = deque(
            [(start, [])]
        )
        visited: Set[SiteId] = {start}
        while queue:
            current, path = queue.popleft()
            for m in self.outgoing(current):
                if m.target == end:
                    return path + [m]
                if m.target not in visited:
                    visited.add(m.target)
                    queue.append((m.target, path + [m]))
        return None

    def sites_of_kind(self, kind: TensorSiteKind) -> List[SiteId]:
        return [sid for sid, k in self.sites.items() if k == kind]

    def sites_in_stratum(self, stratum: TensorStratum) -> List[SiteId]:
        return [
            sid
            for sid, k in self.sites.items()
            if SITE_KIND_STRATUM.get(k) == stratum
        ]


