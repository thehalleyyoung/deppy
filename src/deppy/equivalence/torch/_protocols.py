@dataclass(frozen=True)
class Bug:
    """A concrete bug found in a single tensor program.

    Each bug is localised to a stratum and site in the program's presheaf.
    """

    kind: BugKind
    stratum: TensorStratum
    site_id: SiteId
    description: str
    witness_input: Optional[Any] = None
    observed_output: Optional[Any] = None
    expected_behavior: Optional[str] = None
    repair_hint: Optional[str] = None
    severity: float = 1.0


@dataclass(frozen=True)
class SheafConditionResult:
    """Result of checking whether a single presheaf satisfies the sheaf condition.

    The sheaf condition requires:
    1. **Gluing**: sections that agree on overlaps can be glued into a global section.
    2. **Descent**: cocycle condition g_ij · g_jk = g_ik on triple overlaps.

    A program whose presheaf fails these conditions has internal inconsistencies
    (bugs) even without comparing to a second program.
    """

    satisfies_condition: bool
    gluing_failures: List[Tuple[SiteId, SiteId]]
    descent_failures: List[Tuple[SiteId, SiteId, SiteId]]
    explanation: str = ""


@dataclass(frozen=True)
class SpecificationSection:
    """A single section of a specification presheaf.

    Encodes an expected property at a specific observation site.
    """

    site_id: SiteId
    property_name: str
    expected_value: Any
    tolerance: Optional[float] = None
    predicate: Optional[Callable] = None


@dataclass
class SpecificationPresheaf:
    """Presheaf encoding the expected behaviour of a program.

    Used for correctness checking: a morphism Spec → Impl must exist
    for the implementation to be considered correct.
    """

    name: str
    sections: Dict[SiteId, List[SpecificationSection]] = field(
        default_factory=dict
    )
    strata: Set[TensorStratum] = field(default_factory=set)
    description: str = ""


@dataclass
class AnalysisJudgment:
    """Result of single-program analysis (bug-finding, sheaf-condition check).

    Generalises :class:`TensorGlobalJudgment` from two-program equivalence
    to single-program property checking.
    """

    verdict: AnalysisVerdict
    program: ProgramId
    mode: AnalysisMode
    bugs: List[Bug] = field(default_factory=list)
    sheaf_condition: Optional[SheafConditionResult] = None
    stratum_results: Dict[TensorStratum, AnalysisVerdict] = field(
        default_factory=dict
    )
    obstructions: List[TensorObstruction] = field(default_factory=list)
    explanation: str = ""


@dataclass
class CorrectnessJudgment:
    """Result of checking a program against a specification presheaf.

    A program is correct when a morphism Spec → Impl exists at every site,
    i.e. the implementation's presheaf *refines* the specification.
    """

    verdict: AnalysisVerdict
    program: ProgramId
    specification: str
    violations: List[Bug] = field(default_factory=list)
    conforming_sites: List[SiteId] = field(default_factory=list)
    violating_sites: List[SiteId] = field(default_factory=list)
    explanation: str = ""


