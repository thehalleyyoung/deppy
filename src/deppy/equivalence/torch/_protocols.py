@dataclass(frozen=True)
class TritonBlockSpec:
    """Specification of a Triton block parameter.

    Each block parameter (BLOCK_M, BLOCK_N, …) defines a **cover element**
    in the Grothendieck topology.  The kernel's grid G and block size B
    generate a cover {U_i}_{i=0..G-1} where U_i processes elements
    [i*B .. (i+1)*B) with a mask sieve for the tail.
    """

    name: str  # "BLOCK_M", "BLOCK_N", etc.
    size: int  # block dimension
    constexpr: bool = True
    power_of_two: bool = True


@dataclass(frozen=True)
class TritonMemoryAccessPattern:
    """A Triton load or store pattern.

    Each access is a **section** of the memory presheaf.  The ``mask``
    defines the **sieve** (sub-cover) of valid elements within the block.
    """

    kind: str  # "load" or "store"
    pointer_name: str
    offsets_expr: str  # symbolic offset expression
    mask_expr: Optional[str] = None
    other_value: Optional[str] = None  # default for masked-out elements
    is_volatile: bool = False
    eviction_policy: Optional[str] = None
    cache_modifier: Optional[str] = None


@dataclass(frozen=True)
class TritonReductionSpec:
    """A Triton parallel reduction operation.

    Reductions are critical for equivalence because floating-point addition
    is **not associative**.  Two kernels may reduce in different tree orders,
    producing numerically different results that are still "equivalent"
    under the tolerance model.
    """

    op: str  # "sum", "max", "min", "argmax", "argmin", "xor_sum"
    axis: int
    input_name: str
    is_atomic: bool = False  # uses tl.atomic_add etc.
    keep_dims: bool = False


@dataclass(frozen=True)
class TritonKernelSpec:
    """Full specification of a Triton kernel.

    The kernel spec encodes the **cover** in the Grothendieck topology:
    - ``grid`` defines the number of cover elements
    - ``block_specs`` define the block parameters (site dimensions)
    - ``memory_accesses`` define the sections at each site
    - ``reductions`` define restriction maps (reduction order)
    """

    name: str
    grid: Tuple[Union[int, str], ...] = ()  # grid dimensions
    block_specs: Tuple[TritonBlockSpec, ...] = ()
    num_warps: int = 4
    num_stages: int = 2
    shared_memory_bytes: int = 0
    memory_accesses: Tuple[TritonMemoryAccessPattern, ...] = ()
    reductions: Tuple[TritonReductionSpec, ...] = ()
    constexpr_params: Dict[str, Any] = field(default_factory=dict)
    source_code: Optional[str] = None

    @property
    def block_names(self) -> Tuple[str, ...]:
        return tuple(b.name for b in self.block_specs)

    @property
    def loads(self) -> Tuple[TritonMemoryAccessPattern, ...]:
        return tuple(m for m in self.memory_accesses if m.kind == "load")

    @property
    def stores(self) -> Tuple[TritonMemoryAccessPattern, ...]:
        return tuple(m for m in self.memory_accesses if m.kind == "store")


