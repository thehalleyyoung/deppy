"""Sheaf-theoretic protocols for tensor-program equivalence.

Mathematical framework
─────────────────────
A tensor program  f : Tensor^n → Tensor^m  is modelled as a presheaf

    Sem_f : TensorSite^op  →  RefinementPoset

over a **tensor site category** whose objects are observation sites for the
distinct *strata* of tensor semantics:

  • SHAPE        ─ dimensionality / extent constraints
  • DTYPE        ─ element-type promotion lattice
  • DEVICE       ─ placement topology (CPU / CUDA / MPS)
  • STRIDE       ─ memory-layout / contiguity invariants
  • AUTOGRAD     ─ gradient-computation graph structure
  • NUMERICAL    ─ floating-point value equivalence (within tolerance)
  • KERNEL_CFG   ─ GPU kernel launch configuration
  • TRITON_BLOCK ─ Triton tiling / block-parameter space
  • TRITON_MEM   ─ Triton load / store / mask semantics
  • TRITON_RED   ─ parallel-reduction associativity semantics
  • MLIR_OP      ─ MLIR dialect operation semantics

Morphisms in TensorSite encode how information **restricts** across strata:

    broadcast   : SHAPE × SHAPE  →  SHAPE
    promote     : DTYPE × DTYPE  →  DTYPE
    transfer    : DEVICE × DEVICE →  DEVICE
    chain_rule  : AUTOGRAD(f) × AUTOGRAD(g) →  AUTOGRAD(f∘g)
    tile_refine : KERNEL_CFG  →  TRITON_BLOCK
    lower       : TRITON_BLOCK →  MLIR_OP        (dialect lowering)

Two tensor programs f, g are **equivalent** iff their semantic presheaves
are naturally isomorphic:

    Sem_f ≅ Sem_g   ⟺   ∃ η : Sem_f ⇒ Sem_g  natural iso

with η verified stratum-by-stratum (shape iso, dtype iso, value iso, …)
and glued via the descent condition  H¹(U, Iso(Sem_f, Sem_g)) = 0.

For Triton kernels, the **Grothendieck topology** is generated by the
block-tiling cover: a kernel with grid G and block size B covers the
computation domain via G sites, each covering B elements with a mask
sieve.  Equivalence requires the presheaf over this topology to be
isomorphic.

For MLIR, dialect-lowering functors  L : Dialect_high → Dialect_low
define a chain of categories.  Semantic preservation means L preserves
the presheaf up to natural isomorphism, so equivalence at the high level
implies equivalence at the low level (and we can *lift* low-level proofs
back).
"""

