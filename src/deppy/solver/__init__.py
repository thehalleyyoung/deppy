"""deppy.solver -- SMT and local engine bridges for verification conditions.

This package provides the solver backends that discharge verification
conditions generated by the sheaf-descent type system.  The architecture
is layered:

1. **solver_interface**: Core protocols, result types, and obligation types.
2. **z3_encoder / z3_decoder**: Encode deppy predicates to Z3 and decode
   Z3 models back to deppy values.
3. **z3_backend**: Full Z3 solver wrapper with push/pop and model extraction.
4. **fragment_dispatcher**: Routes obligations by decidability fragment
   to the most efficient solver.
5. **arithmetic_solver**: QF_LIA fragment solver (interval propagation).
6. **boolean_solver**: Propositional fragment solver (DPLL / truth table).
7. **table_solver**: Table-driven solver for finite-domain checks (device,
   dtype compatibility).
8. **proof_certificate**: Proof certificate generation from solver results.
9. **solver_cache**: Query caching and incremental solving.

Usage::

    from deppy.solver import FragmentDispatcher, SolverObligation, SolverConfig

    dispatcher = FragmentDispatcher.create_default(config=SolverConfig(timeout_ms=3000))
    result = dispatcher.dispatch(obligation)
"""

